import { isAxiosError } from 'axios';
import { Loader2, X } from 'lucide-react';
import { useCallback, useEffect, useMemo, useState } from 'react';
import { toast } from 'sonner';

import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select';

import { litellmApi, userPreferencesApi } from '../../api';
import type {
  LiteLlmModelDto,
  ModelDefaultsDto,
  UserPreferencesDtoPreferencesModels,
} from '../../autogenerated';
import { extractApiErrorMessage } from '../../utils/errors';
import { MODEL_SLOTS, type ModelSlotKey } from './modelSlots';

type ModelOverrides = Partial<Record<ModelSlotKey, string | null>>;

export const ModelsPage = () => {
  const [models, setModels] = useState<LiteLlmModelDto[]>([]);
  const [defaults, setDefaults] = useState<ModelDefaultsDto | null>(null);
  const [selectedModels, setSelectedModels] = useState<ModelOverrides>({});
  const [saving, setSaving] = useState(false);
  const [loading, setLoading] = useState(true);

  const loadData = useCallback(async () => {
    setLoading(true);
    try {
      const [modelsResponse, defaultsResponse, userModels] = await Promise.all([
        litellmApi.listModels(),
        litellmApi.getModelDefaults(),
        userPreferencesApi
          .getPreferences()
          .then((res) => (res.data.preferences.models ?? {}) as ModelOverrides)
          .catch((err: unknown) => {
            if (isAxiosError(err) && err.response?.status === 404) {
              return {} as ModelOverrides;
            }
            throw err;
          }),
      ]);
      setModels(modelsResponse.data);
      setDefaults(defaultsResponse.data);
      setSelectedModels(userModels);
    } catch (err) {
      toast.error(
        extractApiErrorMessage(err, 'Failed to load model preferences'),
      );
    } finally {
      setLoading(false);
    }
  }, []);

  useEffect(() => {
    loadData();
  }, [loadData]);

  const getSource = (slotKey: ModelSlotKey): 'User' | 'Default' => {
    const value = selectedModels[slotKey];
    if (value) return 'User';
    return 'Default';
  };

  const nonEmbeddingModels = useMemo(
    () => models.filter((m) => !m.supportsEmbedding),
    [models],
  );
  const embeddingModels = useMemo(
    () => models.filter((m) => m.supportsEmbedding),
    [models],
  );

  const handleModelChange = (slotKey: ModelSlotKey, value: string) => {
    setSelectedModels((prev) => ({ ...prev, [slotKey]: value }));
  };

  const handleClear = (slotKey: ModelSlotKey) => {
    setSelectedModels((prev) => ({ ...prev, [slotKey]: null }));
  };

  const handleSave = async () => {
    setSaving(true);
    try {
      const cleanModels: UserPreferencesDtoPreferencesModels = {};
      for (const slot of MODEL_SLOTS) {
        const value = selectedModels[slot.key];
        if (value) {
          cleanModels[slot.key] = value;
        } else {
          cleanModels[slot.key] = null;
        }
      }

      await userPreferencesApi.updatePreferences({ models: cleanModels });
      toast.success('Model preferences saved');
    } catch (err) {
      toast.error(
        extractApiErrorMessage(err, 'Failed to save model preferences'),
      );
    } finally {
      setSaving(false);
    }
  };

  if (loading) {
    return (
      <div className="flex items-center justify-center py-12">
        <Loader2 className="h-6 w-6 animate-spin text-muted-foreground" />
      </div>
    );
  }

  return (
    <div className="space-y-6">
      <div>
        <h2 className="text-base font-semibold">Model Preferences</h2>
        <p className="text-sm text-muted-foreground mt-1">
          Override the default LLM models for your account. Project-level
          settings take priority over these.
        </p>
      </div>

      {MODEL_SLOTS.map((slot) => {
        const source = getSource(slot.key);
        const filteredModels = slot.embeddingOnly
          ? embeddingModels
          : nonEmbeddingModels;
        const currentValue = selectedModels[slot.key];

        return (
          <div key={slot.key} className="space-y-1.5">
            <div className="flex items-center gap-2">
              <span className="text-sm font-medium">{slot.label}</span>
              <Badge
                variant={source === 'User' ? 'default' : 'secondary'}
                className="text-[10px] px-1.5 py-0">
                {source}
              </Badge>
            </div>
            <p className="text-xs text-muted-foreground">{slot.description}</p>
            <div className="flex items-center gap-2">
              <Select
                value={currentValue ?? ''}
                onValueChange={(val) => handleModelChange(slot.key, val)}>
                <SelectTrigger className="w-80">
                  <SelectValue
                    placeholder={
                      defaults?.[slot.key]
                        ? `Use default (${defaults[slot.key]})`
                        : 'Use default'
                    }
                  />
                </SelectTrigger>
                <SelectContent>
                  {filteredModels.map((model) => (
                    <SelectItem key={model.id} value={model.id}>
                      {model.id}
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
              {currentValue && (
                <Button
                  variant="ghost"
                  size="sm"
                  className="h-8 w-8 p-0"
                  onClick={() => handleClear(slot.key)}>
                  <X className="w-4 h-4 text-muted-foreground" />
                </Button>
              )}
            </div>
          </div>
        );
      })}

      <Button onClick={handleSave} disabled={saving}>
        {saving && <Loader2 className="w-4 h-4 animate-spin mr-1" />}
        Save
      </Button>
    </div>
  );
};
