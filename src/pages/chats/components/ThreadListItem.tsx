import { MoreHorizontal, Network, Trash2 } from 'lucide-react';
import React, { useState } from 'react';

import type { ThreadDto, ThreadDtoStatusEnum } from '../../../autogenerated';
import {
  AlertDialog,
  AlertDialogAction,
  AlertDialogCancel,
  AlertDialogContent,
  AlertDialogDescription,
  AlertDialogFooter,
  AlertDialogHeader,
  AlertDialogTitle,
} from '../../../components/ui/alert-dialog';
import { Avatar, AvatarFallback } from '../../../components/ui/avatar';
import { Badge } from '../../../components/ui/badge';
import { Button } from '../../../components/ui/button';
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuTrigger,
} from '../../../components/ui/dropdown-menu';
import { cn } from '../../../components/ui/utils';
import { formatRelativeDate } from '../../../utils/formatRelativeDate';
import {
  getStatusBadgeClass,
  getThreadStatusDisplay,
} from '../../../utils/threadStatus';
import type { DraftThread, GraphCacheEntry } from '../types';
import { getAgentColor, getAgentInitials } from '../utils/chatsPageUtils';

interface ThreadListItemProps {
  thread: ThreadDto | DraftThread;
  isActive: boolean;
  graphCache: Record<string, GraphCacheEntry>;
  onSelect: (threadId: string) => void;
  onDeleteThread: (threadId: string) => void;
  agents: {
    nodeId: string;
    label: string;
    description?: string;
  }[];
}

export const ThreadListItem: React.FC<ThreadListItemProps> = React.memo(
  ({ thread, isActive, graphCache, onSelect, onDeleteThread, agents }) => {
    const [deleteConfirmOpen, setDeleteConfirmOpen] = useState(false);
    const isDraft = 'isDraft' in thread && thread.isDraft === true;
    const statusMeta = isDraft
      ? { label: 'Draft', color: '#d9d9d9' }
      : getThreadStatusDisplay(
          (thread as ThreadDto).status as ThreadDtoStatusEnum,
        );
    const graphName = graphCache[thread.graphId]?.graph.name;

    return (
      <>
        <div
          onClick={() => onSelect(thread.id)}
          className={cn(
            'group mx-1 px-3 py-2.5 rounded-lg cursor-pointer transition-colors',
            isActive ? 'bg-primary/10' : 'hover:bg-muted/60',
          )}>
          {/* Row 1: name + status badge + options dropdown */}
          <div className="flex items-center gap-1.5 mb-1">
            <span
              className={cn(
                'text-sm font-medium truncate min-w-0',
                isActive ? 'text-primary' : 'text-foreground',
              )}>
              {thread.name || 'New Thread'}
            </span>
            <div className="flex-1" />
            {statusMeta && (
              <Badge
                className={cn(
                  'text-[10px] px-1.5 py-0 h-4 flex-shrink-0 font-medium',
                  isDraft
                    ? getStatusBadgeClass('draft')
                    : getStatusBadgeClass((thread as ThreadDto).status),
                )}>
                {statusMeta.label}
              </Badge>
            )}
            {!isDraft && (
              <DropdownMenu>
                <DropdownMenuTrigger asChild>
                  <Button
                    variant="ghost"
                    size="icon"
                    className="opacity-0 group-hover:opacity-100 transition-opacity h-5 w-5 shrink-0"
                    onClick={(e) => e.stopPropagation()}>
                    <MoreHorizontal className="h-3.5 w-3.5" />
                  </Button>
                </DropdownMenuTrigger>
                <DropdownMenuContent align="end">
                  <DropdownMenuItem
                    variant="destructive"
                    onClick={(e) => {
                      e.stopPropagation();
                      setDeleteConfirmOpen(true);
                    }}>
                    <Trash2 className="h-3.5 w-3.5" />
                    Delete
                  </DropdownMenuItem>
                </DropdownMenuContent>
              </DropdownMenu>
            )}
          </div>

          {/* Row 2: Network icon + graph name */}
          <p className="text-[11px] text-muted-foreground mb-1.5 flex items-center gap-1">
            <Network className="w-3 h-3 flex-shrink-0" />
            <span className="truncate">{graphName || 'Loading\u2026'}</span>
          </p>

          {/* Row 3: agent avatars + relative timestamp */}
          <div className="flex items-center justify-between">
            <div className="flex items-center gap-1">
              {agents.slice(0, 3).map((agent) => (
                <Avatar key={agent.nodeId} className="w-5 h-5">
                  <AvatarFallback
                    className={`${getAgentColor(agent.nodeId)} text-white text-[9px]`}>
                    {getAgentInitials(agent.label)}
                  </AvatarFallback>
                </Avatar>
              ))}
              {agents.length > 3 && (
                <span className="text-[10px] text-muted-foreground ml-0.5">
                  +{agents.length - 3}
                </span>
              )}
            </div>
            <span className="text-[10px] text-muted-foreground flex-shrink-0">
              {formatRelativeDate(thread.createdAt)}
            </span>
          </div>
        </div>

        <AlertDialog
          open={deleteConfirmOpen}
          onOpenChange={setDeleteConfirmOpen}>
          <AlertDialogContent>
            <AlertDialogHeader>
              <AlertDialogTitle>Delete thread</AlertDialogTitle>
              <AlertDialogDescription>
                Are you sure you want to delete this thread?
              </AlertDialogDescription>
            </AlertDialogHeader>
            <AlertDialogFooter>
              <AlertDialogCancel>Cancel</AlertDialogCancel>
              <AlertDialogAction
                className="bg-destructive text-white hover:bg-destructive/90"
                onClick={() => onDeleteThread(thread.id)}>
                Delete
              </AlertDialogAction>
            </AlertDialogFooter>
          </AlertDialogContent>
        </AlertDialog>
      </>
    );
  },
);

ThreadListItem.displayName = 'ThreadListItem';
