import { Loader2 } from 'lucide-react';
import type { FC } from 'react';

import type { LiteLlmModelDto } from '../../../autogenerated';
import { MarkdownContent } from '../../../components/markdown/MarkdownContent';
import { Button } from '../../../components/ui/button';
import {
  Dialog,
  DialogContent,
  DialogFooter,
  DialogHeader,
  DialogTitle,
} from '../../../components/ui/dialog';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '../../../components/ui/select';
import { Textarea } from '../../../components/ui/textarea';

interface AnalyzeThreadModalProps {
  open: boolean;
  onCancel: () => void;
  onAnalyze: () => void;
  loading: boolean;
  disabled: boolean;
  result: string | null;
  userInput: string;
  onUserInputChange: (value: string) => void;
  model: string | undefined;
  onModelChange: (value: string | undefined) => void;
  models: LiteLlmModelDto[];
  modelsLoading: boolean;
}

export const AnalyzeThreadModal: FC<AnalyzeThreadModalProps> = ({
  open,
  onCancel,
  onAnalyze,
  loading,
  disabled,
  result,
  userInput,
  onUserInputChange,
  model,
  onModelChange,
  models,
  modelsLoading,
}) => {
  return (
    <Dialog open={open} onOpenChange={(isOpen) => !isOpen && onCancel()}>
      <DialogContent className="sm:max-w-lg">
        <DialogHeader>
          <DialogTitle>Analyze thread</DialogTitle>
        </DialogHeader>
        <div className="flex flex-col gap-3">
          <p className="text-sm text-foreground">
            AI will analyze this thread and share hints about improvements or
            potential problems.
          </p>
          <div>
            <span className="font-medium text-sm block mb-1.5">Model</span>
            <Select
              value={model ?? ''}
              onValueChange={(value) =>
                onModelChange(value === '' ? undefined : value)
              }
              disabled={disabled || loading}>
              <SelectTrigger className="w-full">
                <SelectValue
                  placeholder={
                    modelsLoading ? 'Loading models...' : 'Select model'
                  }
                />
              </SelectTrigger>
              <SelectContent>
                {modelsLoading && (
                  <div className="flex items-center justify-center p-2">
                    <Loader2 className="h-4 w-4 animate-spin" />
                  </div>
                )}
                {!modelsLoading && models.length === 0 && (
                  <div className="p-2 text-sm text-muted-foreground text-center">
                    No models available
                  </div>
                )}
                {models.map((m) => (
                  <SelectItem key={m.id} value={m.id}>
                    {m.ownedBy ? `${m.id} (${m.ownedBy})` : m.id}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          </div>
          <Textarea
            rows={4}
            placeholder="Optional context or questions for the analysis"
            value={userInput}
            onChange={(e) => onUserInputChange(e.target.value)}
            disabled={disabled || loading}
          />
          {result ? (
            <div className="max-h-[360px] overflow-y-auto p-3 border border-border rounded-lg">
              <MarkdownContent content={result} />
            </div>
          ) : (
            <p className="text-sm text-muted-foreground">
              Run the analysis to see AI feedback for this thread.
            </p>
          )}
        </div>
        <DialogFooter>
          <Button variant="outline" onClick={onCancel}>
            Close
          </Button>
          <Button onClick={onAnalyze} disabled={disabled || loading}>
            {loading && <Loader2 className="h-4 w-4 animate-spin" />}
            Analyze
          </Button>
        </DialogFooter>
      </DialogContent>
    </Dialog>
  );
};
