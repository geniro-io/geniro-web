import { Loader2 } from 'lucide-react';
import React, { useMemo } from 'react';

import type { GraphDto } from '../../../autogenerated';
import { GraphDtoStatusEnum } from '../../../autogenerated';
import { Badge } from '../../../components/ui/badge';
import { cn } from '../../../components/ui/utils';
import {
  getGraphStatusBadgeClass,
  GRAPH_STATUS_META,
} from '../utils/chatsPageUtils';

interface GraphPickerContentProps {
  graphs: GraphDto[];
  loading: boolean;
  error: string | null;
  onSelect: (graphId: string) => void;
}

export const GraphPickerContent: React.FC<GraphPickerContentProps> = React.memo(
  ({ graphs, loading, error, onSelect }) => {
    const content = useMemo(() => {
      if (loading) {
        return (
          <div className="p-3 text-center">
            <Loader2 className="h-4 w-4 animate-spin mx-auto text-muted-foreground" />
          </div>
        );
      }
      if (error) {
        return <span className="text-xs text-muted-foreground">{error}</span>;
      }
      if (graphs.length === 0) {
        return (
          <span className="text-xs text-muted-foreground">
            No graphs available
          </span>
        );
      }
      return (
        <div className="max-h-[300px] overflow-y-auto flex flex-col">
          {graphs.map((graph) => {
            const isRunning = graph.status === GraphDtoStatusEnum.Running;
            return (
              <button
                key={graph.id}
                type="button"
                className="text-left px-2.5 py-1.5 rounded-md w-full hover:bg-muted/60 transition-colors flex flex-col items-start"
                onClick={() => onSelect(graph.id)}>
                <div className="flex items-center gap-1.5 w-full">
                  <span className="text-xs font-medium truncate flex-1 min-w-0 text-foreground">
                    {graph.name && graph.name.trim().length > 0
                      ? graph.name
                      : `Graph ${graph.id.slice(-6)}`}
                  </span>
                  <Badge
                    className={cn(
                      'text-[9px] px-1 py-0 h-3.5 flex-shrink-0 font-medium',
                      getGraphStatusBadgeClass(graph.status),
                    )}>
                    {GRAPH_STATUS_META[graph.status]?.label ?? graph.status}
                  </Badge>
                </div>
                <span className="text-[10px] text-muted-foreground leading-tight">
                  v{graph.version}
                  {graph.createdAt && (
                    <>
                      {' \u00b7 '}
                      {new Date(graph.createdAt).toLocaleDateString()}
                    </>
                  )}
                </span>
                {!isRunning && (
                  <span className="text-[10px] text-amber-500 leading-tight">
                    Must be running to send messages
                  </span>
                )}
              </button>
            );
          })}
        </div>
      );
    }, [error, graphs, loading, onSelect]);

    return content;
  },
);

GraphPickerContent.displayName = 'GraphPickerContent';
