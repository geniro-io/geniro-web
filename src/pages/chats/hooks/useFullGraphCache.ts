import { useCallback, useEffect, useRef, useState } from 'react';

import { graphsApi } from '../../../api';
import type { GraphDto, ThreadDto } from '../../../autogenerated';
import { useWebSocketEvent } from '../../../hooks/useWebSocket';
import type {
  GraphPreviewNotification,
  GraphUpdateNotification,
} from '../../../services/WebSocketTypes';
import type { DraftThread, FullGraphCacheEntry } from '../types';

const FULL_GRAPH_TTL_MS = 5 * 60 * 1000; // 5 minutes

interface UseFullGraphCacheDeps {
  selectedThread: ThreadDto | DraftThread | undefined;
  selectedThreadIsDraft: boolean;
}

export const useFullGraphCache = (deps: UseFullGraphCacheDeps) => {
  const { selectedThread, selectedThreadIsDraft } = deps;

  const [cache, setCache] = useState<Record<string, FullGraphCacheEntry>>({});
  const inflightRef = useRef<Set<string>>(new Set());
  const selectedThreadRef = useRef(selectedThread);
  const selectedThreadIsDraftRef = useRef(selectedThreadIsDraft);

  useEffect(() => {
    selectedThreadRef.current = selectedThread;
  }, [selectedThread]);
  useEffect(() => {
    selectedThreadIsDraftRef.current = selectedThreadIsDraft;
  }, [selectedThreadIsDraft]);

  const fetchFullGraph = useCallback(async (graphId: string) => {
    if (inflightRef.current.has(graphId)) return;
    inflightRef.current.add(graphId);
    try {
      const response = await graphsApi.findGraphById(graphId);
      const graph: GraphDto = response.data;
      setCache((prev) => ({
        ...prev,
        [graphId]: { graph, fetchedAt: Date.now() },
      }));
    } catch (error) {
      console.warn('Failed to fetch full graph for cache', graphId, error);
    } finally {
      inflightRef.current.delete(graphId);
    }
  }, []);

  const invalidate = useCallback((graphId: string) => {
    setCache((prev) => {
      if (!prev[graphId]) return prev;
      const next = { ...prev };
      delete next[graphId];
      return next;
    });
  }, []);

  const invalidateAndRefetchIfActive = useCallback(
    (graphId: string) => {
      invalidate(graphId);
      const current = selectedThreadRef.current;
      const isDraft = selectedThreadIsDraftRef.current;
      if (current && !isDraft && current.graphId === graphId) {
        void fetchFullGraph(graphId);
      }
    },
    [invalidate, fetchFullGraph],
  );

  // Auto-fetch when selected thread changes
  useEffect(() => {
    if (!selectedThread || selectedThreadIsDraft) return;
    const graphId = selectedThread.graphId;

    // Read cache inside setState updater to avoid adding 'cache' to deps
    // (which would cause re-triggering on every cache update).
    // The updater never modifies state — it only checks freshness and kicks off a fetch.
    setCache((prev) => {
      const entry = prev[graphId];
      const isValid = entry && Date.now() - entry.fetchedAt < FULL_GRAPH_TTL_MS;
      if (!isValid) {
        void fetchFullGraph(graphId);
      }
      return prev;
    });
  }, [selectedThread, selectedThreadIsDraft, fetchFullGraph]);

  // Invalidate on graph.preview events (schema changed)
  useWebSocketEvent('graph.preview', (notification) => {
    const data = notification as GraphPreviewNotification;
    if (data.graphId) {
      invalidateAndRefetchIfActive(data.graphId);
    }
  });

  // Invalidate on graph.update events (schema may have changed)
  useWebSocketEvent('graph.update', (notification) => {
    const data = notification as GraphUpdateNotification;
    if (data.graphId) {
      invalidateAndRefetchIfActive(data.graphId);
    }
  });

  const getFullGraph = useCallback(
    (graphId: string): GraphDto | undefined => {
      const entry = cache[graphId];
      if (!entry) return undefined;
      if (Date.now() - entry.fetchedAt >= FULL_GRAPH_TTL_MS) return undefined;
      return entry.graph;
    },
    [cache],
  );

  return {
    fullGraphCache: cache,
    getFullGraph,
  };
};
