import { FolderGit2, Plus, Search } from 'lucide-react';
import { useEffect, useState } from 'react';
import { toast } from 'sonner';

import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import {
  Dialog,
  DialogContent,
  DialogFooter,
  DialogHeader,
  DialogTitle,
} from '@/components/ui/dialog';
import { Input } from '@/components/ui/input';
import { RepoCard, type Repository } from '@/components/ui/repo-card';
import { Skeleton } from '@/components/ui/skeleton';

import { gitRepositoriesApi } from '../../api';
import type { GitRepositoryDto, RepoIndexDto } from '../../autogenerated';
import { extractApiErrorMessage } from '../../utils/errors';
import { CreateRepositoryModal } from './components/CreateRepositoryModal';

interface RepoWithIndex extends GitRepositoryDto {
  index?: RepoIndexDto;
}

function toRepository(r: RepoWithIndex): Repository {
  return {
    id: r.id,
    owner: r.owner,
    repo: r.repo,
    url: r.url,
    provider: r.provider as 'GITHUB',
    defaultBranch: r.defaultBranch,
    createdAt: r.createdAt,
    index: r.index
      ? {
          status: r.index.status,
          lastIndexedCommit: r.index.lastIndexedCommit,
          indexedTokens: r.index.indexedTokens,
          estimatedTokens: r.index.estimatedTokens,
          errorMessage: r.index.errorMessage,
          updatedAt: r.index.updatedAt,
        }
      : undefined,
  };
}

export const RepositoriesListPage = () => {
  const [loading, setLoading] = useState(true);
  const [deleting, setDeleting] = useState<string | null>(null);
  const [reindexing, setReindexing] = useState<string | null>(null);
  const [repositories, setRepositories] = useState<RepoWithIndex[]>([]);
  const [error, setError] = useState<string | null>(null);
  const [searchTerm, setSearchTerm] = useState('');
  const [isCreateModalOpen, setIsCreateModalOpen] = useState(false);
  const [deleteTarget, setDeleteTarget] = useState<Repository | null>(null);

  const fetchRepositories = async () => {
    try {
      const [reposRes, indexesRes] = await Promise.all([
        gitRepositoriesApi.getRepositories(),
        gitRepositoriesApi.getRepoIndexes(),
      ]);

      const repos = reposRes.data || [];
      const indexes = indexesRes.data || [];

      const reposWithIndexes: RepoWithIndex[] = repos.map((repo) => ({
        ...repo,
        index: indexes.find(
          (idx) =>
            idx.repositoryId === repo.id && idx.branch === repo.defaultBranch,
        ),
      }));

      setRepositories(reposWithIndexes);
    } catch (e) {
      console.error('Error fetching repositories:', e);
      const errorMessage = extractApiErrorMessage(
        e,
        'Failed to load repositories',
      );
      setError(errorMessage);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    (async () => {
      await fetchRepositories();
    })();
  }, []);

  const handleDeleteRepository = async (repositoryId: string) => {
    setDeleting(repositoryId);
    try {
      await gitRepositoriesApi.deleteRepository(repositoryId);
      setRepositories((prev) =>
        prev.filter((repo) => repo.id !== repositoryId),
      );
      toast.success('Repository deleted successfully');
    } catch (e: unknown) {
      console.error('Error deleting repository:', e);
      const errorMessage = extractApiErrorMessage(
        e,
        'Failed to delete repository',
      );
      toast.error(errorMessage);
    } finally {
      setDeleting(null);
    }
  };

  const handleReindex = async (repositoryId: string) => {
    setReindexing(repositoryId);
    try {
      await gitRepositoriesApi.triggerReindex({ repositoryId });
      toast.success('Reindex triggered successfully');
      const [repoRes, indexRes] = await Promise.all([
        gitRepositoriesApi.getRepositoryById(repositoryId),
        gitRepositoriesApi.getRepoIndexByRepositoryId(repositoryId),
      ]);
      const repo = repoRes.data;
      const index = indexRes.data as RepoIndexDto | undefined;
      setRepositories((prev) =>
        prev.map((r) => (r.id === repositoryId ? { ...repo, index } : r)),
      );
    } catch (e: unknown) {
      console.error('Error triggering reindex:', e);
      const errorMessage = extractApiErrorMessage(
        e,
        'Failed to trigger reindex',
      );
      toast.error(errorMessage);
    } finally {
      setReindexing(null);
    }
  };

  const filteredRepositories = repositories.filter((repo) => {
    if (!searchTerm) return true;
    const lowerSearch = searchTerm.toLowerCase();
    return (
      repo.owner?.toLowerCase().includes(lowerSearch) ||
      repo.repo?.toLowerCase().includes(lowerSearch) ||
      repo.url?.toLowerCase().includes(lowerSearch)
    );
  });

  const stats = repositories.reduce(
    (acc, repo) => {
      acc.total += 1;
      const status = repo.index?.status;
      if (status === 'completed') {
        acc.indexed += 1;
      } else if (status === 'in_progress') {
        acc.indexing += 1;
      } else if (status === 'failed') {
        acc.failed += 1;
      }
      return acc;
    },
    { total: 0, indexed: 0, indexing: 0, failed: 0 },
  );

  if (loading) {
    return (
      <div className="p-6">
        <div className="mb-6">
          <Skeleton className="h-8 w-48 mb-2" />
          <Skeleton className="h-5 w-72" />
        </div>
        <div
          className="grid gap-6"
          style={{
            gridTemplateColumns: 'repeat(auto-fill, minmax(320px, 1fr))',
          }}>
          {Array.from({ length: 3 }).map((_, i) => (
            <Skeleton key={i} className="h-52 rounded-xl" />
          ))}
        </div>
      </div>
    );
  }

  if (error) {
    return (
      <div className="p-6 text-center">
        <p className="text-destructive">{error}</p>
      </div>
    );
  }

  return (
    <div className="min-h-full">
      <div className="p-6">
        <div className="flex justify-between gap-6 flex-wrap mb-6">
          <div>
            <h1 className="text-2xl font-semibold text-foreground mb-1">
              Git Repositories
            </h1>
            <p className="text-muted-foreground">
              Manage repositories and track indexing progress
            </p>
          </div>
          <Button className="gap-2" onClick={() => setIsCreateModalOpen(true)}>
            <Plus className="w-4 h-4" />
            Add Repository
          </Button>
        </div>

        <div className="flex flex-wrap gap-8 items-center mb-6">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
            <Input
              placeholder="Search repositories..."
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
              className="pl-9 w-[340px] max-w-full"
            />
          </div>
          <div className="flex items-center gap-2">
            <Badge variant="secondary" className="gap-1.5">
              Total <span className="font-bold">{stats.total}</span>
            </Badge>
            <Badge
              variant="outline"
              className="gap-1.5 text-emerald-600 border-emerald-200 bg-emerald-50">
              Indexed <span className="font-bold">{stats.indexed}</span>
            </Badge>
            <Badge
              variant="outline"
              className="gap-1.5 text-blue-600 border-blue-200 bg-blue-50">
              Indexing <span className="font-bold">{stats.indexing}</span>
            </Badge>
            {stats.failed > 0 && (
              <Badge
                variant="outline"
                className="gap-1.5 text-red-600 border-red-200 bg-red-50">
                Failed <span className="font-bold">{stats.failed}</span>
              </Badge>
            )}
          </div>
        </div>
      </div>

      {repositories.length === 0 ? (
        <div className="flex flex-col items-center justify-center py-16 text-center">
          <FolderGit2 className="w-12 h-12 text-muted-foreground/30 mb-4" />
          <p className="text-sm font-medium text-muted-foreground mb-4">
            No repositories found
          </p>
          <Button onClick={() => setIsCreateModalOpen(true)}>
            Add your first repository
          </Button>
        </div>
      ) : filteredRepositories.length === 0 ? (
        <div className="flex flex-col items-center justify-center py-16 text-center">
          <FolderGit2 className="w-12 h-12 text-muted-foreground/30 mb-4" />
          <p className="text-sm text-muted-foreground">
            No repositories match &quot;{searchTerm}&quot;
          </p>
        </div>
      ) : (
        <div
          className="grid gap-6 px-6 pb-6"
          style={{
            gridTemplateColumns: 'repeat(auto-fill, minmax(320px, 1fr))',
          }}>
          {filteredRepositories.map((repo) => (
            <RepoCard
              key={repo.id}
              repo={toRepository(repo)}
              onDelete={(r) => setDeleteTarget(r)}
              onReindex={handleReindex}
              reindexing={reindexing}
            />
          ))}
        </div>
      )}

      <CreateRepositoryModal
        open={isCreateModalOpen}
        onClose={() => setIsCreateModalOpen(false)}
        onSuccess={fetchRepositories}
      />

      <Dialog
        open={!!deleteTarget}
        onOpenChange={(open) => !open && setDeleteTarget(null)}>
        <DialogContent className="sm:max-w-[425px]">
          <DialogHeader>
            <DialogTitle>Delete repository</DialogTitle>
          </DialogHeader>
          <p className="text-sm text-muted-foreground py-2">
            Are you sure you want to delete{' '}
            <span className="font-medium text-foreground">
              {deleteTarget?.owner}/{deleteTarget?.repo}
            </span>
            ? This action cannot be undone.
          </p>
          <DialogFooter className="gap-2">
            <Button variant="outline" onClick={() => setDeleteTarget(null)}>
              Cancel
            </Button>
            <Button
              variant="destructive"
              onClick={() => {
                if (deleteTarget) {
                  handleDeleteRepository(deleteTarget.id);
                  setDeleteTarget(null);
                }
              }}
              disabled={deleting === deleteTarget?.id}>
              Delete
            </Button>
          </DialogFooter>
        </DialogContent>
      </Dialog>
    </div>
  );
};
