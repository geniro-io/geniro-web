import { Check, Loader2 } from 'lucide-react';
import { useState } from 'react';
import { useNavigate } from 'react-router';
import { toast } from 'sonner';

import { Button } from '@/components/ui/button';
import { Card } from '@/components/ui/card';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Skeleton } from '@/components/ui/skeleton';
import { Textarea } from '@/components/ui/textarea';

import { graphsApi, projectsApi } from '../../api';
import type {
  CreateGraphDto,
  CreateProjectDto,
  ProjectDto,
  UpdateProjectDto,
} from '../../autogenerated';
import { useProjectContext } from '../../contexts/ProjectContext';
import { extractApiErrorMessage } from '../../utils/errors';

interface ProjectFormValues {
  name: string;
  description?: string;
  icon?: string;
}

interface GraphFormValues {
  name: string;
  description?: string;
}

const DEFAULT_PROJECT_COLORS = [
  '#3B82F6',
  '#10B981',
  '#F59E0B',
  '#EF4444',
  '#8B5CF6',
  '#EC4899',
  '#06B6D4',
  '#84CC16',
];

export const CreateProjectPage = () => {
  const navigate = useNavigate();
  const { loading: projectsLoading, loadProjects } = useProjectContext();

  const [currentStep, setCurrentStep] = useState<0 | 1>(0);
  const [createdProject, setCreatedProject] = useState<ProjectDto | null>(null);
  const [colorValue, setColorValue] = useState<string>('#3B82F6');
  const [saving, setSaving] = useState(false);
  const [projectValues, setProjectValues] = useState<ProjectFormValues>({
    name: '',
    description: '',
    icon: '',
  });
  const [graphValues, setGraphValues] = useState<GraphFormValues>({
    name: '',
    description: '',
  });

  const handleProjectSubmit = async () => {
    if (!projectValues.name.trim()) return;

    try {
      setSaving(true);

      if (createdProject) {
        const updatePayload: UpdateProjectDto = {
          name: projectValues.name.trim(),
          description: projectValues.description?.trim() || null,
          icon: projectValues.icon?.trim() || null,
          color: colorValue || null,
        };
        const res = await projectsApi.updateProject(
          createdProject.id,
          updatePayload,
        );
        await loadProjects();
        setCreatedProject(res.data);
      } else {
        const createPayload: CreateProjectDto = {
          name: projectValues.name.trim(),
          description: projectValues.description?.trim() || null,
          icon: projectValues.icon?.trim() || null,
          color: colorValue || null,
        };
        const res = await projectsApi.createProject(createPayload);
        await loadProjects();
        setCreatedProject(res.data);
      }

      setCurrentStep(1);
    } catch (err: unknown) {
      if (typeof err === 'object' && err !== null && 'errorFields' in err) {
        return;
      }
      toast.error(
        extractApiErrorMessage(
          err,
          createdProject
            ? 'Failed to update project'
            : 'Failed to create project',
        ),
      );
    } finally {
      setSaving(false);
    }
  };

  const handleGraphSubmit = async () => {
    if (!createdProject) return;
    if (!graphValues.name.trim()) return;

    try {
      setSaving(true);

      const graphPayload: CreateGraphDto = {
        name: graphValues.name.trim(),
        description: graphValues.description?.trim() || null,
        schema: { nodes: [], edges: [] },
      };

      const res = await graphsApi.createGraph(graphPayload, {
        headers: { 'x-project-id': createdProject.id },
      });

      navigate(`/projects/${createdProject.id}/graphs/${res.data.id}`, {
        replace: true,
      });
    } catch (err: unknown) {
      if (typeof err === 'object' && err !== null && 'errorFields' in err) {
        return;
      }
      toast.error(extractApiErrorMessage(err, 'Failed to create graph'));
    } finally {
      setSaving(false);
    }
  };

  const handleBack = () => {
    if (createdProject) {
      setProjectValues({
        name: createdProject.name,
        description: createdProject.description ?? '',
        icon: createdProject.icon ?? '',
      });
      setColorValue(createdProject.color ?? '#3B82F6');
    }
    setCurrentStep(0);
  };

  if (projectsLoading) {
    return (
      <div className="min-h-screen min-w-[1248px] flex items-center justify-center bg-muted/50 p-6">
        <Skeleton className="h-[500px] w-[520px] rounded-2xl" />
      </div>
    );
  }

  return (
    <div className="min-h-screen min-w-[1248px] flex items-center justify-center bg-muted/50 p-6">
      <Card className="w-full max-w-[520px] rounded-2xl shadow-lg p-8">
        {/* Step Indicator */}
        <div className="flex items-center justify-center gap-3 mb-8">
          <div className="flex items-center gap-2">
            <div
              className={`w-8 h-8 rounded-full flex items-center justify-center text-sm font-medium ${
                currentStep >= 0
                  ? 'bg-primary text-primary-foreground'
                  : 'bg-muted text-muted-foreground'
              }`}>
              {currentStep > 0 ? <Check className="w-4 h-4" /> : '1'}
            </div>
            <span
              className={`text-sm font-medium ${
                currentStep === 0 ? 'text-foreground' : 'text-muted-foreground'
              }`}>
              Project
            </span>
          </div>
          <div className="w-12 h-px bg-border" />
          <div className="flex items-center gap-2">
            <div
              className={`w-8 h-8 rounded-full flex items-center justify-center text-sm font-medium ${
                currentStep >= 1
                  ? 'bg-primary text-primary-foreground'
                  : 'bg-muted text-muted-foreground'
              }`}>
              2
            </div>
            <span
              className={`text-sm font-medium ${
                currentStep === 1 ? 'text-foreground' : 'text-muted-foreground'
              }`}>
              First Graph
            </span>
          </div>
        </div>

        {currentStep === 0 && (
          <>
            <div className="text-center mb-7">
              <div className="text-[40px] mb-2">🚀</div>
              <h2 className="text-xl font-semibold text-foreground mb-2">
                Create your first project
              </h2>
              <p className="text-muted-foreground text-sm">
                Projects help you organize your graphs, knowledge, and
                repositories.
              </p>
            </div>

            <div className="space-y-4">
              <div className="space-y-2">
                <Label>
                  <span className="text-destructive">* </span>Project name
                </Label>
                <Input
                  value={projectValues.name}
                  onChange={(e) =>
                    setProjectValues({ ...projectValues, name: e.target.value })
                  }
                  placeholder="My First Project"
                  className="h-10"
                  autoFocus
                />
              </div>
              <div className="space-y-2">
                <Label>Description</Label>
                <Textarea
                  value={projectValues.description || ''}
                  onChange={(e) =>
                    setProjectValues({
                      ...projectValues,
                      description: e.target.value,
                    })
                  }
                  placeholder="Describe what this project is for"
                  rows={3}
                />
              </div>
              <div className="space-y-2">
                <Label>Icon</Label>
                <Input
                  value={projectValues.icon || ''}
                  onChange={(e) =>
                    setProjectValues({ ...projectValues, icon: e.target.value })
                  }
                  placeholder="🚀"
                />
                <p className="text-xs text-muted-foreground">
                  Emoji or icon slug, e.g. 🚀
                </p>
              </div>
              <div className="space-y-2">
                <Label>Color</Label>
                <div className="flex flex-wrap gap-2">
                  {DEFAULT_PROJECT_COLORS.map((color) => (
                    <button
                      key={color}
                      type="button"
                      className="w-8 h-8 rounded-lg border-2 transition-all"
                      style={{
                        backgroundColor: color,
                        borderColor:
                          colorValue === color ? color : 'transparent',
                        boxShadow:
                          colorValue === color
                            ? `0 0 0 2px white, 0 0 0 4px ${color}`
                            : 'none',
                      }}
                      onClick={() => setColorValue(color)}
                    />
                  ))}
                </div>
                <p className="text-xs text-muted-foreground">
                  Accent color for this project
                </p>
              </div>
              <Button
                className="w-full h-10 mt-2"
                onClick={handleProjectSubmit}
                disabled={!projectValues.name.trim() || saving}>
                {saving && <Loader2 className="w-4 h-4 animate-spin mr-1" />}
                {createdProject ? 'Update & Continue' : 'Create Project'}
              </Button>
            </div>
          </>
        )}

        {currentStep === 1 && (
          <>
            <div className="text-center mb-7">
              <div className="text-[40px] mb-2">📊</div>
              <h2 className="text-xl font-semibold text-foreground mb-2">
                Create your first graph
              </h2>
              <p className="text-muted-foreground text-sm">
                A graph is a visual workflow where you wire together AI agents,
                tools, and triggers.
              </p>
            </div>

            <div className="space-y-4">
              <div className="space-y-2">
                <Label>
                  <span className="text-destructive">* </span>Graph name
                </Label>
                <Input
                  value={graphValues.name}
                  onChange={(e) =>
                    setGraphValues({ ...graphValues, name: e.target.value })
                  }
                  placeholder="My First Graph"
                  className="h-10"
                  autoFocus
                />
              </div>
              <div className="space-y-2">
                <Label>Description</Label>
                <Textarea
                  value={graphValues.description || ''}
                  onChange={(e) =>
                    setGraphValues({
                      ...graphValues,
                      description: e.target.value,
                    })
                  }
                  placeholder="What will this graph do?"
                  rows={3}
                />
              </div>
              <div className="flex justify-between mt-2">
                <Button variant="outline" className="h-10" onClick={handleBack}>
                  Back
                </Button>
                <Button
                  className="h-10"
                  onClick={handleGraphSubmit}
                  disabled={!graphValues.name.trim() || saving}>
                  {saving && <Loader2 className="w-4 h-4 animate-spin mr-1" />}
                  Create Graph
                </Button>
              </div>
            </div>
          </>
        )}
      </Card>
    </div>
  );
};
