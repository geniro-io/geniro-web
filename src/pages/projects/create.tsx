import {
  App,
  Button,
  Card,
  ColorPicker,
  Form,
  Input,
  Spin,
  Typography,
} from 'antd';
import { useEffect, useState } from 'react';
import { useNavigate } from 'react-router';

import { projectsApi } from '../../api';
import type { CreateProjectDto } from '../../autogenerated';
import { useProjectContext } from '../../contexts/ProjectContext';
import { extractApiErrorMessage } from '../../utils/errors';

const { Title, Paragraph } = Typography;

interface ProjectFormValues {
  name: string;
  description?: string;
  icon?: string;
}

const DEFAULT_PROJECT_COLORS = [
  '#3B82F6',
  '#10B981',
  '#F59E0B',
  '#EF4444',
  '#8B5CF6',
  '#EC4899',
  '#06B6D4',
  '#84CC16',
];

export const CreateProjectPage = () => {
  const { message } = App.useApp();
  const navigate = useNavigate();
  const {
    projects,
    loading: projectsLoading,
    loadProjects,
  } = useProjectContext();
  const [form] = Form.useForm<ProjectFormValues>();
  const [saving, setSaving] = useState(false);
  const [colorValue, setColorValue] = useState<string>('#3B82F6');

  // If user already has projects, redirect to the projects list
  useEffect(() => {
    if (!projectsLoading && projects.length > 0) {
      navigate('/projects', { replace: true });
    }
  }, [projects, projectsLoading, navigate]);

  const handleSubmit = async () => {
    try {
      const values = await form.validateFields();
      setSaving(true);

      const payload: CreateProjectDto = {
        name: values.name.trim(),
        description: values.description?.trim() || null,
        icon: values.icon?.trim() || null,
        color: colorValue || null,
      };

      const res = await projectsApi.createProject(payload);
      await loadProjects();
      message.success('Project created! Welcome aboard.');
      navigate(`/projects/${res.data.id}/graphs`, { replace: true });
    } catch (err) {
      if (typeof err === 'object' && err !== null && 'errorFields' in err) {
        return;
      }
      message.error(extractApiErrorMessage(err, 'Failed to create project'));
    } finally {
      setSaving(false);
    }
  };

  if (projectsLoading) {
    return (
      <div
        style={{
          display: 'flex',
          justifyContent: 'center',
          alignItems: 'center',
          height: '100vh',
        }}>
        <Spin size="large" />
      </div>
    );
  }

  return (
    <div
      style={{
        minHeight: '100vh',
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        background: '#f5f5f5',
        padding: 24,
      }}>
      <Card
        style={{
          width: '100%',
          maxWidth: 480,
          borderRadius: 16,
          boxShadow: '0 4px 24px rgba(0,0,0,0.08)',
        }}
        styles={{ body: { padding: 32 } }}>
        <div style={{ textAlign: 'center', marginBottom: 28 }}>
          <div style={{ fontSize: 40, marginBottom: 8 }}>ðŸš€</div>
          <Title level={3} style={{ marginBottom: 8 }}>
            Create your first project
          </Title>
          <Paragraph type="secondary" style={{ marginBottom: 0 }}>
            Projects help you organize your graphs, knowledge, and repositories.
          </Paragraph>
        </div>

        <Form form={form} layout="vertical" onFinish={handleSubmit}>
          <Form.Item
            name="name"
            label="Project name"
            rules={[{ required: true, message: 'Project name is required' }]}>
            <Input placeholder="My First Project" size="large" autoFocus />
          </Form.Item>
          <Form.Item name="description" label="Description">
            <Input.TextArea
              rows={3}
              placeholder="Describe what this project is for"
            />
          </Form.Item>
          <Form.Item
            name="icon"
            label="Icon"
            extra="Emoji or icon slug, e.g. ðŸš€">
            <Input placeholder="ðŸš€" />
          </Form.Item>
          <Form.Item label="Color" extra="Accent color for this project">
            <ColorPicker
              value={colorValue}
              onChange={(color) => setColorValue(color.toHexString())}
              presets={[
                {
                  label: 'Recommended',
                  colors: DEFAULT_PROJECT_COLORS,
                },
              ]}
              showText
            />
          </Form.Item>
          <Form.Item style={{ marginBottom: 0, marginTop: 8 }}>
            <Button
              type="primary"
              htmlType="submit"
              size="large"
              loading={saving}
              style={{ width: '100%' }}>
              Create Project
            </Button>
          </Form.Item>
        </Form>
      </Card>
    </div>
  );
};
