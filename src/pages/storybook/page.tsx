/* ── LiveThreadSection component ────────────────────────────────────────── */

function LiveThreadSection() {
  const [items, setItems] = useState<RenderedItem[]>([]);
  const [running, setRunning] = useState(false);
  const cancelRef = useRef(false);
  const bottomRef = useRef<HTMLDivElement>(null);

  // Auto-scroll to bottom only while the simulation is running
  useEffect(() => {
    if (running) {
      bottomRef.current?.scrollIntoView({
        behavior: 'smooth',
        block: 'nearest',
      });
    }
  }, [items, running]);

  const play = useCallback(async () => {
    cancelRef.current = false;
    setItems([]);
    setRunning(true);

    const wait = (ms: number) =>
      new Promise<void>((r) => {
        setTimeout(r, ms);
      });

    for (const step of LIVE_STEPS) {
      if (cancelRef.current) break;

      if (step.kind === 'reasoning') {
        const chunks = step.chunks;
        const chunkDelay = step.chunkDelayMs ?? 500;

        // Add with first chunk, streaming = true
        setItems((prev) => [
          ...prev,
          { kind: 'reasoning', content: chunks[0], streaming: true },
        ]);

        // Deliver remaining chunks
        for (let i = 1; i < chunks.length; i++) {
          await wait(chunkDelay);
          if (cancelRef.current) break;
          const accumulated = chunks.slice(0, i + 1).join('');
          setItems((prev) => {
            const next = [...prev];
            const last = next[next.length - 1];
            if (last.kind === 'reasoning') {
              next[next.length - 1] = { ...last, content: accumulated };
            }
            return next;
          });
        }
      } else {
        // Set streaming=false on last reasoning item (triggers flush via hook)
        setItems((prev) => {
          const next = [...prev];
          const last = next[next.length - 1];
          if (last?.kind === 'reasoning' && last.streaming) {
            next[next.length - 1] = { ...last, streaming: false };
          }
          return next;
        });

        if (step.kind === 'tool') {
          setItems((prev) => [
            ...prev,
            {
              kind: 'tool',
              toolName: step.toolName,
              args: step.args,
              status: 'running',
            },
          ]);
          await wait(step.runMs ?? 1500);

          if (!cancelRef.current) {
            setItems((prev) => {
              const next = [...prev];
              next[next.length - 1] = {
                kind: 'tool',
                toolName: step.toolName,
                args: step.args,
                status: 'done',
                result: step.result,
                tokens: step.tokens,
              };
              return next;
            });
          }
        } else if (step.kind === 'shell') {
          setItems((prev) => [
            ...prev,
            {
              kind: 'shell',
              command: step.command,
              stdout: '',
              exitCode: 0,
              status: 'executing',
            },
          ]);
          await wait(step.runMs ?? 2000);

          if (!cancelRef.current) {
            setItems((prev) => {
              const next = [...prev];
              next[next.length - 1] = {
                kind: 'shell',
                command: step.command,
                stdout: step.stdout,
                stderr: step.stderr,
                exitCode: step.exitCode,
                status: 'executed',
                tokens: step.tokens,
              };
              return next;
            });
          }
        } else if (step.kind === 'chat') {
          setItems((prev) => [
            ...prev,
            {
              kind: 'chat',
              sender: step.sender,
              agentRole: step.agentRole,
              color: step.color,
              content: step.content,
              tokens: step.tokens,
            },
          ]);
        }
      }

      // Gap between steps
      if (!cancelRef.current) {
        await wait(STEP_GAP);
      }
    }

    // Mark any trailing reasoning as done
    setItems((prev) => {
      const next = [...prev];
      const last = next[next.length - 1];
      if (last?.kind === 'reasoning' && last.streaming) {
        next[next.length - 1] = { ...last, streaming: false };
      }
      return next;
    });

    setRunning(false);
  }, []);

  const reset = useCallback(() => {
    cancelRef.current = true;
    setItems([]);
    setRunning(false);
  }, []);

  return (
    <Section
      id="live-thread"
      title="Live Thread"
      description="Simulates real-time socket notifications: reasoning arrives as 3 chunks and reveals over 3 s (adapting speed per chunk); non-reasoning steps flush remaining reasoning in 500 ms. Tool calls transition running → done.">
      <div className="px-5 py-4">
        <div className="flex items-center gap-2 mb-4">
          <Button
            size="sm"
            onClick={running ? reset : play}
            variant={running ? 'outline' : 'default'}>
            {running ? (
              <>
                <X className="w-3.5 h-3.5 mr-1" /> Reset
              </>
            ) : (
              <>
                <Zap className="w-3.5 h-3.5 mr-1" /> Play
              </>
            )}
          </Button>
          {!running && items.length > 0 && (
            <Button size="sm" variant="ghost" onClick={reset}>
              Clear
            </Button>
          )}
          <span className="text-xs text-muted-foreground">
            {running
              ? 'Streaming...'
              : items.length > 0
                ? `${items.length} messages`
                : 'Click Play to simulate live thread'}
          </span>
        </div>

        <div className="w-full max-w-2xl space-y-2">
          {/* User message that kicks things off */}
          {items.length > 0 && (
            <ChatBubble
              sender="You"
              role="human"
              content="Run a security audit on the authentication module."
              timestamp="3:16 PM"
              color="bg-gray-500"
            />
          )}

          {items.map((item, idx) => {
            if (item.kind === 'reasoning') {
              return (
                <StreamingReasoningBlock
                  key={`r-${idx}`}
                  content={item.content}
                  isStreaming={item.streaming}
                />
              );
            }
            if (item.kind === 'tool') {
              return (
                <ToolBlock
                  key={`t-${idx}`}
                  toolName={item.toolName}
                  status={item.status}
                  args={item.args}
                  result={item.result}
                  tokens={item.tokens}
                />
              );
            }
            if (item.kind === 'shell') {
              return (
                <ShellBlock
                  key={`s-${idx}`}
                  command={item.command}
                  stdout={item.stdout}
                  stderr={item.stderr}
                  exitCode={item.exitCode}
                  status={item.status}
                  tokens={item.tokens}
                />
              );
            }
            if (item.kind === 'chat') {
              return (
                <ChatBubble
                  key={`c-${idx}`}
                  sender={item.sender}
                  role="ai"
                  agentRole={item.agentRole}
                  color={item.color}
                  content={item.content}
                  tokens={item.tokens}
                />
              );
            }
            return null;
          })}
          <div ref={bottomRef} />
        </div>
      </div>
    </Section>
  );
}

