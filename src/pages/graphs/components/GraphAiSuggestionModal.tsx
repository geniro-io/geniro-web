import { createTwoFilesPatch } from 'diff';
import { AlertTriangle } from 'lucide-react';
import { useMemo } from 'react';

import type {
  LiteLlmModelDto,
  SuggestGraphInstructionsResponseDtoUpdatesInner,
} from '../../../autogenerated';
import { DiffHtmlView } from '../../../components/markdown/DiffHtmlView';
import { Alert, AlertTitle } from '../../../components/ui/alert';
import { Avatar, AvatarFallback } from '../../../components/ui/avatar';
import { Badge } from '../../../components/ui/badge';
import { Button } from '../../../components/ui/button';
import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
} from '../../../components/ui/dialog';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '../../../components/ui/select';
import { Textarea } from '../../../components/ui/textarea';
import { getAgentInitials } from '../../chats/utils/chatsPageUtils';
import type { GraphNode, GraphNodeData } from '../types';

type GraphAiSuggestionModalProps = {
  open: boolean;
  agentNodes: GraphNode[];
  selectedNodeId: string | null;
  updatesByNodeId: Record<
    string,
    SuggestGraphInstructionsResponseDtoUpdatesInner
  >;
  userRequest: string;
  loading: boolean;
  canSubmit: boolean;
  showRunningWarning: boolean;
  models: LiteLlmModelDto[];
  modelsLoading: boolean;
  selectedModel?: string;
  onClose: () => void;
  onSelectNode: (nodeId: string) => void;
  onUserRequestChange: (value: string) => void;
  onModelChange: (value?: string) => void;
  onSubmit: () => void;
  onApply: () => void;
  getCurrentInstructions: (node: GraphNode) => string;
};

const getNodeData = (node: GraphNode): GraphNodeData =>
  node.data as unknown as GraphNodeData;

export const GraphAiSuggestionModal = ({
  open,
  agentNodes,
  selectedNodeId,
  updatesByNodeId,
  userRequest,
  loading,
  canSubmit,
  showRunningWarning,
  models,
  modelsLoading,
  selectedModel,
  onClose,
  onSelectNode,
  onUserRequestChange,
  onModelChange,
  onSubmit,
  onApply,
  getCurrentInstructions,
}: GraphAiSuggestionModalProps) => {
  const selectedNode =
    agentNodes.find((node) => node.id === selectedNodeId) ?? null;
  const selectedUpdate = selectedNodeId
    ? updatesByNodeId[selectedNodeId]
    : undefined;
  const hasUpdates = Object.keys(updatesByNodeId).length > 0;

  const diffText = useMemo(() => {
    if (!selectedNode || !selectedUpdate) return '';
    const current = getCurrentInstructions(selectedNode);
    const suggested = selectedUpdate.instructions ?? '';
    const diffString = createTwoFilesPatch(
      'Current',
      'Suggested',
      current,
      suggested,
      '',
      '',
      { context: Number.MAX_SAFE_INTEGER },
    );
    return diffString.trimEnd();
  }, [getCurrentInstructions, selectedNode, selectedUpdate]);

  if (!open) {
    return null;
  }

  return (
    <Dialog open={open} onOpenChange={(openState) => !openState && onClose()}>
      <DialogContent className="sm:max-w-[1200px] max-h-[90vh] overflow-y-auto overflow-x-hidden">
        <DialogHeader>
          <DialogTitle>Improve workflow with AI</DialogTitle>
        </DialogHeader>
        <div className="flex flex-col gap-5">
          {showRunningWarning && (
            <Alert>
              <AlertTriangle className="size-4" />
              <AlertTitle>Start the graph to use AI suggestions</AlertTitle>
            </Alert>
          )}

          {/* Agent selection */}
          <div>
            <span className="text-sm font-semibold block mb-2">Agents</span>
            {agentNodes.length === 0 ? (
              <span className="text-sm text-muted-foreground">
                No agent nodes in this graph
              </span>
            ) : (
              <div className="grid grid-cols-3 gap-3">
                {agentNodes.map((node) => {
                  const data = getNodeData(node);
                  const isSelected = node.id === selectedNodeId;
                  const hasUpdate = Boolean(updatesByNodeId[node.id]);
                  const kind = data.templateKind ?? 'Agent';
                  return (
                    <div
                      key={node.id}
                      onClick={() => onSelectNode(node.id)}
                      className={[
                        'cursor-pointer rounded-lg overflow-hidden transition-all',
                        'border bg-card',
                        isSelected
                          ? 'border-primary shadow-[0_4px_12px_color-mix(in_srgb,var(--color-primary)_25%,transparent)]'
                          : hasUpdate
                            ? 'border-primary/40'
                            : 'border-border',
                        'hover:border-primary/60 hover:shadow-sm',
                      ]
                        .filter(Boolean)
                        .join(' ')}>
                      <div className="bg-muted px-3 py-2.5 border-b border-border flex items-center justify-between gap-2">
                        <div className="flex items-center gap-2 min-w-0">
                          <Avatar className="size-5 flex-shrink-0">
                            <AvatarFallback className="bg-primary/20 text-primary text-[8px] font-semibold">
                              {getAgentInitials(data.label || 'Agent')}
                            </AvatarFallback>
                          </Avatar>
                          <span
                            className="font-semibold text-sm truncate text-foreground"
                            style={{ maxWidth: 180 }}>
                            {data.label || `Node ${node.id.slice(-6)}`}
                          </span>
                        </div>
                        {hasUpdate && (
                          <Badge
                            variant="outline"
                            className="border-primary/40 text-primary text-[10px] flex-shrink-0">
                            Updated
                          </Badge>
                        )}
                      </div>
                      <div className="p-2.5 flex gap-1.5 flex-wrap">
                        <Badge className="bg-primary hover:bg-primary text-primary-foreground text-[10px] px-2 py-0 h-5">
                          {kind}
                        </Badge>
                        <Badge
                          variant="outline"
                          className="text-[10px] border-primary/50 text-primary px-2 py-0 h-5">
                          {data.template}
                        </Badge>
                      </div>
                    </div>
                  );
                })}
              </div>
            )}
          </div>

          {/* Model selector */}
          <div>
            <span className="text-sm font-semibold block mb-1.5">Model</span>
            <Select
              value={selectedModel ?? ''}
              onValueChange={(val) => onModelChange(val || undefined)}>
              <SelectTrigger className="w-full">
                <SelectValue
                  placeholder={
                    modelsLoading ? 'Loading models...' : 'Select model'
                  }
                />
              </SelectTrigger>
              <SelectContent>
                {models.map((model) => (
                  <SelectItem key={model.id} value={model.id}>
                    {model.ownedBy
                      ? `${model.id} (${model.ownedBy})`
                      : model.id}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          </div>

          {/* User request */}
          <div>
            <span className="text-sm font-semibold block mb-1.5">
              What should be improved?
            </span>
            <Textarea
              value={userRequest}
              onChange={(e) => onUserRequestChange(e.target.value)}
              placeholder="Describe the workflow improvements for all agents"
              rows={3}
            />
            <div className="mt-2 flex justify-end">
              <Button
                onClick={onSubmit}
                disabled={!canSubmit || loading || !userRequest.trim()}>
                {loading ? (
                  <span className="flex items-center gap-2">
                    <span className="size-4 animate-spin rounded-full border-2 border-current border-t-transparent" />
                    Send
                  </span>
                ) : (
                  'Send'
                )}
              </Button>
            </div>
          </div>

          {/* Suggested instructions diff */}
          <div>
            <span className="text-sm font-semibold block mb-1.5">
              Suggested instructions
            </span>
            {selectedNode && selectedUpdate ? (
              <div
                className="rounded-md border border-border overflow-auto"
                style={{ maxHeight: 420 }}>
                <DiffHtmlView diff={diffText} wrapLines />
              </div>
            ) : (
              <span className="text-sm text-muted-foreground">
                {selectedNode
                  ? 'No updates for this agent yet.'
                  : 'Select an agent to view changes.'}
              </span>
            )}
          </div>

          {/* Footer actions */}
          <div className="flex justify-end gap-2 pt-1 border-t border-border">
            <Button variant="outline" onClick={onClose}>
              Close
            </Button>
            <Button onClick={onApply} disabled={!hasUpdates}>
              Apply updates
            </Button>
          </div>
        </div>
      </DialogContent>
    </Dialog>
  );
};
