import type { ThreadMessageDtoRequestTokenUsage } from '../../../../autogenerated';
import {
  Popover,
  PopoverContent,
  PopoverTrigger,
} from '../../../../components/ui/popover';
import {
  formatRequestTokenCount,
  formatRequestUsdShort,
} from './threadMessagesViewUtils';
import { TokenUsagePopoverIcon } from './TokenUsagePopoverIcon';

export interface ShellToolDisplayProps {
  name: string;
  status: 'calling' | 'executed' | 'stopped';
  resultContent?: unknown;
  shellCommand?: string;
  toolOptions?: Record<string, JsonValue>;
  title?: string;
  metadata?: { nodeId?: string; createdAt?: string; roleLabel?: string };
  /**
   * Request token usage for the shell tool call input (AI message).
   */
  requestTokenUsageIn?: ThreadMessageDtoRequestTokenUsage | null;
  /**
   * Request token usage for the shell tool call output (tool result message).
   */
  requestTokenUsageOut?: ThreadMessageDtoRequestTokenUsage | null;
  /** LLM request duration in milliseconds. */
  durationMs?: number;
}

const ansiUp = (() => {
  const instance = new AnsiUp();
  // Keep HTML escaping enabled (defense-in-depth).
  // Newer versions use `escape_html`, older used `escape_for_html`.
  (instance as unknown as Record<string, unknown>).escape_html = true;
  (instance as unknown as Record<string, unknown>).escape_for_html = true;
  return instance;
})();

const normalizeShellText = (text: string): string =>
  text.replace(/\r\n/g, '\n').replace(/\r/g, '\n');

const containsAnsi = (text: string): boolean => {
  // We intentionally avoid regex literals with control chars here because of
  // eslint's `no-control-regex` rule.
  return text.includes('\u001b[') || text.includes('\u009b[');
};

const stripAnsiSgr = (text: string): string => {
  const esc = '\u001b';
  const csi = '\u009b';
  const sgr = new RegExp(`(?:${esc}|${csi})\\[[0-9;]*m`, 'g');
  return text.replace(sgr, '');
};

const renderAnsiHtml = (text: string): string =>
  ansiUp.ansi_to_html(normalizeShellText(text));

const parseJsonSafe = (value: string): JsonValue | null => {
  try {
    return JSON.parse(value) as JsonValue;
  } catch {
    return null;
  }
};

const renderToolPopoverContent = (
  value: unknown,
  toolOptions?: Record<string, JsonValue>,
  toolLabel?: string,
  requestTokenUsageIn?: ThreadMessageDtoRequestTokenUsage | null,
  requestTokenUsageOut?: ThreadMessageDtoRequestTokenUsage | null,
  durationMs?: number,
): React.ReactNode => {
  let parsed: JsonValue | null = null;
  if (typeof value === 'string') {
    parsed = parseJsonSafe(value);
  } else if (
    value !== null &&
    (typeof value === 'object' ||
      typeof value === 'number' ||
      typeof value === 'boolean')
  ) {
    parsed = value as JsonValue;
  }

  const containerStyle: React.CSSProperties = { maxWidth: 520 };
  const innerStyle: React.CSSProperties = {
    maxHeight: 300,
    overflow: 'auto',
    background: '#f5f5f5',
    border: '1px solid #eee',
    borderRadius: 6,
    padding: 12,
    fontFamily:
      'ui-monospace, SFMono-Regular, Menlo, Monaco, Consolas, "Courier New", monospace',
    fontSize: 12,
    whiteSpace: 'pre-wrap',
  };

  const sectionStyle: React.CSSProperties = {
    marginBottom: 16,
  };

  const sectionTitleStyle: React.CSSProperties = {
    fontWeight: 'bold',
    fontSize: 13,
    marginBottom: 8,
    color: '#333',
    borderBottom: '1px solid #ddd',
    paddingBottom: 4,
  };

  const hasRequestTokenUsage = requestTokenUsageIn || requestTokenUsageOut;

  return (
    <div style={containerStyle}>
      {(toolLabel && toolLabel.trim().length > 0) || hasRequestTokenUsage ? (
        <div
          style={{
            marginBottom: 12,
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'space-between',
            gap: 8,
          }}>
          {toolLabel && toolLabel.trim().length > 0 ? (
            <span style={{ fontWeight: 600, fontSize: 13 }}>
              Tool: {toolLabel}
            </span>
          ) : (
            <span />
          )}
          {hasRequestTokenUsage && (
            <TokenUsagePopoverIcon
              requestTokenUsageIn={requestTokenUsageIn}
              requestTokenUsageOut={requestTokenUsageOut}
              durationMs={durationMs}
            />
          )}
        </div>
      ) : null}
      {toolOptions && Object.keys(toolOptions).length > 0 && (
        <div style={sectionStyle}>
          <div style={sectionTitleStyle}>Tool Options:</div>
          <div style={innerStyle}>
            <JsonView value={toolOptions} style={lightTheme} />
          </div>
        </div>
      )}

      {(parsed || (value !== undefined && value !== null)) && (
        <div style={sectionStyle}>
          <div style={sectionTitleStyle}>Output:</div>
          <div style={innerStyle}>
            {parsed ? (
              <JsonView value={parsed as object} style={lightTheme} />
            ) : (
              <pre
                style={{
                  margin: 0,
                  whiteSpace: 'pre-wrap',
                  wordBreak: 'break-word',
                }}>
                {typeof value === 'string' && containsAnsi(value) ? (
                  <span
                    dangerouslySetInnerHTML={{ __html: renderAnsiHtml(value) }}
                  />
                ) : (
                  String(value ?? '')
                )}
              </pre>
            )}
          </div>
        </div>
      )}
    </div>
  );
};

const truncateToLines = (
  text: string,
  maxLines: number,
): { truncated: string; full: string; isTruncated: boolean } => {
  const lines = text.split('\n');
  const isTruncated = lines.length > maxLines;
  const truncated = lines.slice(0, maxLines).join('\n');
  return { truncated, full: text, isTruncated };
};

export const ShellToolDisplay: React.FC<ShellToolDisplayProps> = ({
  name,
  status,
  resultContent,
  shellCommand,
  toolOptions,
  title,
  requestTokenUsageIn,
  requestTokenUsageOut,
  durationMs,
}) => {
  const [commandExpanded, setCommandExpanded] = useState(false);
  const [outputExpanded, setOutputExpanded] = useState(false);

  const resultObj = isPlainObject(resultContent)
    ? (resultContent as Record<string, unknown>)
    : null;

  const toolErrorText = useMemo(() => {
    if (!resultObj) return undefined;
    const errorValue = resultObj.error;
    if (typeof errorValue === 'string') {
      const trimmed = errorValue.trim();
      return trimmed.length > 0 ? trimmed : undefined;
    }
    if (errorValue === null || errorValue === undefined) return undefined;
    try {
      const serialized = JSON.stringify(errorValue, null, 2);
      const trimmed = serialized.trim();
      return trimmed.length > 0 ? trimmed : undefined;
    } catch {
      const asString = String(errorValue);
      const trimmed = asString.trim();
      return trimmed.length > 0 ? trimmed : undefined;
    }
  }, [resultObj]);
  const hasToolError = Boolean(toolErrorText);

  const stdoutCandidate = resultObj?.stdout;
  const stdoutText =
    typeof stdoutCandidate === 'string'
      ? normalizeShellText(stdoutCandidate)
      : null;
  const stderrCandidate = resultObj?.stderr;
  const stderrText =
    typeof stderrCandidate === 'string'
      ? normalizeShellText(stderrCandidate)
      : null;
  const rawStringResult =
    typeof resultContent === 'string'
      ? normalizeShellText(resultContent)
      : null;
  const outputCandidate = resultObj?.output;
  const outputFieldText =
    typeof outputCandidate === 'string'
      ? normalizeShellText(outputCandidate)
      : null;
  const focusResultCandidate = resultObj?.focusResult;
  const focusResultText =
    typeof focusResultCandidate === 'string' && focusResultCandidate.trim()
      ? normalizeShellText(focusResultCandidate)
      : null;
  const outputFocusCandidate = toolOptions?.outputFocus;
  const outputFocusText =
    typeof outputFocusCandidate === 'string' && outputFocusCandidate.trim()
      ? outputFocusCandidate.trim()
      : null;
  const exitCodeCandidate = resultObj?.exitCode;
  const exitCode =
    typeof exitCodeCandidate === 'number' ? exitCodeCandidate : null;
  const exitCodeColor =
    hasToolError || (exitCode !== null && exitCode !== 0)
      ? '#ff4d4f'
      : '#9d9d9d';
  const tint = hasToolError
    ? '#2b1d1d'
    : exitCode === null
      ? '#2b2b2b'
      : exitCode === 0
        ? '#1d2b1f'
        : '#2b1d1d';

  const toolNameText = useMemo(() => {
    const displayName = title && title.trim().length > 0 ? title.trim() : name;
    if (displayName !== name) {
      return displayName;
    }
    return `${displayName}${
      toolOptions?.purpose ? ` | ${String(toolOptions.purpose)}` : ''
    }`;
  }, [name, title, toolOptions?.purpose]);

  const getOutputText = (): string | null => {
    if (outputFieldText) return outputFieldText;
    if (rawStringResult) return rawStringResult;
    if (resultObj) {
      const displayText =
        resultObj.output || resultObj.stdout || resultObj.stderr;
      if (typeof displayText === 'string') {
        return normalizeShellText(displayText);
      }
    }
    return null;
  };

  const outputText = getOutputText();
  const commandTruncated = shellCommand
    ? truncateToLines(shellCommand, 3)
    : undefined;
  const stdoutTruncated = stdoutText
    ? truncateToLines(stdoutText, 3)
    : undefined;
  const stderrTruncated = stderrText
    ? truncateToLines(stderrText, 3)
    : undefined;
  const fallbackOutputTruncated =
    !stdoutText && !stderrText && outputText
      ? truncateToLines(outputText, 3)
      : undefined;
  const isAnyOutputTruncated =
    stdoutTruncated?.isTruncated ||
    stderrTruncated?.isTruncated ||
    fallbackOutputTruncated?.isTruncated;

  const outputTextForCopy = useMemo(
    () =>
      [
        stdoutText ? `STDOUT:\n${stripAnsiSgr(stdoutText)}` : null,
        stderrText ? `STDERR:\n${stripAnsiSgr(stderrText)}` : null,
        !stdoutText && !stderrText && outputText
          ? stripAnsiSgr(outputText)
          : null,
      ]
        .filter(Boolean)
        .join('\n\n'),
    [outputText, stderrText, stdoutText],
  );

  const renderPlainOutputText = (
    text: string,
    color: string,
    truncatedInfo?: ReturnType<typeof truncateToLines>,
  ) => (
    <SyntaxHighlighter
      language="bash"
      style={vscDarkPlus}
      customStyle={{
        margin: 0,
        padding: 0,
        background: 'transparent',
        fontSize: '12px',
      }}
      PreTag="div"
      codeTagProps={{
        style: { fontFamily: 'inherit', color },
      }}>
      {outputExpanded || !truncatedInfo?.isTruncated
        ? text
        : (truncatedInfo?.truncated ?? text)}
    </SyntaxHighlighter>
  );

  const renderAnsiOutputText = (
    text: string,
    color: string,
    truncatedInfo?: ReturnType<typeof truncateToLines>,
  ) => {
    const displayText =
      outputExpanded || !truncatedInfo?.isTruncated
        ? text
        : (truncatedInfo?.truncated ?? text);

    return (
      <pre
        style={{
          margin: 0,
          fontSize: '12px',
          color,
          whiteSpace: 'pre-wrap',
          wordBreak: 'break-word',
          lineHeight: '1.5',
        }}>
        <span
          dangerouslySetInnerHTML={{ __html: renderAnsiHtml(displayText) }}
        />
      </pre>
    );
  };

  const renderOutputBlock = (
    label: string,
    text: string,
    color: string,
    truncatedInfo?: ReturnType<typeof truncateToLines>,
  ) => (
    <div style={{ marginBottom: 8 }}>
      <div
        style={{
          display: 'flex',
          alignItems: 'center',
          gap: 6,
          marginBottom: 4,
        }}>
        <span
          style={{
            fontSize: '11px',
            color: '#c4c4c4',
            letterSpacing: 0.5,
            textTransform: 'uppercase',
          }}>
          {label}
        </span>
      </div>
      {containsAnsi(text)
        ? renderAnsiOutputText(text, color, truncatedInfo)
        : renderPlainOutputText(text, color, truncatedInfo)}
    </div>
  );

  const formatRequestTokenUsageLine = (
    usage?: ThreadMessageDtoRequestTokenUsage | null,
  ): string => {
    if (!usage) return '\u2014';
    const tokens = formatRequestTokenCount(usage.totalTokens);
    const cost = formatRequestUsdShort(usage.totalPrice);
    return `${tokens} (${cost})`;
  };

  const hasRequestTokens =
    (requestTokenUsageIn &&
      typeof requestTokenUsageIn.totalTokens === 'number') ||
    (requestTokenUsageOut &&
      typeof requestTokenUsageOut.totalTokens === 'number');

  const shouldShowTokens = Boolean(hasRequestTokens);

  const toolHeaderSuffix = hasToolError ? toolErrorText : undefined;
  const toolHeaderTitle = toolHeaderSuffix
    ? `${toolNameText} - ${toolHeaderSuffix}`
    : toolNameText;

  const containerStyle: React.CSSProperties = {
    background: '#1e1e1e',
    borderRadius: 6,
    border: `1px solid ${hasToolError ? '#5c2c2c' : '#333'}`,
    color: '#e8e8e8',
    fontFamily:
      'ui-monospace, SFMono-Regular, Menlo, Monaco, Consolas, "Courier New", monospace',
    fontSize: 12,
    display: 'flex',
    flexDirection: 'column',
    gap: 6,
    boxShadow: 'inset 0 0 0 1px rgba(255,255,255,0.02)',
    backgroundImage: `linear-gradient(${tint}, ${tint})`,
    backgroundBlendMode: 'soft-light',
  };

  return (
    <div>
      <div style={containerStyle}>
        <div
          style={{
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'space-between',
            gap: 8,
            padding: '5px 10px',
            background: '#1a1a1a',
          }}>
          {(status === 'executed' && resultContent !== undefined) ||
          (toolOptions && Object.keys(toolOptions).length > 0) ? (
            <Popover>
              <PopoverTrigger asChild>
                <div
                  style={{
                    cursor: 'pointer',
                    overflow: 'hidden',
                    textOverflow: 'ellipsis',
                    whiteSpace: 'nowrap',
                    flex: 1,
                    minWidth: 0,
                  }}
                  aria-label={
                    status === 'executed'
                      ? `View shell result for ${toolHeaderTitle}`
                      : status === 'stopped'
                        ? `Shell ${toolHeaderTitle} is stopped`
                        : `Shell ${toolHeaderTitle} is calling`
                  }
                  title={toolHeaderTitle}>
                  <span>{toolNameText}</span>
                  {toolHeaderSuffix && (
                    <span style={{ color: '#ff4d4f' }}>
                      {' '}
                      - {toolHeaderSuffix}
                    </span>
                  )}
                </div>
              </PopoverTrigger>
              <PopoverContent
                align="start"
                className="w-auto max-w-[560px] p-4">
                {renderToolPopoverContent(
                  resultContent,
                  toolOptions,
                  name,
                  requestTokenUsageIn,
                  requestTokenUsageOut,
                  durationMs,
                )}
              </PopoverContent>
            </Popover>
          ) : (
            <div
              style={{
                overflow: 'hidden',
                textOverflow: 'ellipsis',
                whiteSpace: 'nowrap',
                flex: 1,
                minWidth: 0,
              }}
              title={toolHeaderTitle}>
              <span>{toolNameText}</span>
              {toolHeaderSuffix && (
                <span style={{ color: '#ff4d4f' }}> - {toolHeaderSuffix}</span>
              )}
            </div>
          )}

          {status === 'calling' ? (
            <span
              style={{
                color: '#c4c4c4',
                animation:
                  'messages-tab-thinking-pulse 1.6s ease-in-out infinite',
              }}>
              executing...
            </span>
          ) : status === 'stopped' ? (
            <span style={{ color: '#ff4d4f' }}>stopped</span>
          ) : (
            <span
              style={{
                color: exitCodeColor,
                whiteSpace: 'nowrap',
                fontSize: '11px',
              }}>
              executed{exitCode !== null ? ` | exit ${exitCode}` : ''}
            </span>
          )}
        </div>

        {shellCommand && (
          <div style={{ padding: '5px 10px' }}>
            <div style={{ display: 'flex', alignItems: 'flex-start' }}>
              <span
                style={{
                  color: '#a0a0a0',
                  paddingRight: '10px',
                  flexShrink: 0,
                }}>
                $
              </span>
              <div style={{ flex: 1, minWidth: 0 }}>
                <div
                  style={{
                    maxHeight:
                      commandExpanded || !commandTruncated?.isTruncated
                        ? 'none'
                        : '4.5em',
                    overflow:
                      commandExpanded || !commandTruncated?.isTruncated
                        ? 'visible'
                        : 'hidden',
                    position: 'relative',
                  }}>
                  <SyntaxHighlighter
                    language="bash"
                    style={vscDarkPlus}
                    customStyle={{
                      margin: 0,
                      padding: 0,
                      background: 'transparent',
                      fontSize: '12px',
                      whiteSpace: 'pre-wrap',
                      wordBreak: 'break-word',
                      lineHeight: '1.5',
                    }}
                    PreTag="div"
                    codeTagProps={{
                      style: {
                        fontFamily: 'inherit',
                        whiteSpace: 'pre-wrap',
                        wordBreak: 'break-word',
                        margin: 0,
                        padding: 0,
                        lineHeight: '1.5',
                      },
                    }}>
                    {shellCommand}
                  </SyntaxHighlighter>
                  {commandTruncated?.isTruncated && !commandExpanded && (
                    <div
                      style={{
                        position: 'absolute',
                        bottom: 0,
                        left: 0,
                        right: 0,
                        height: '1.5em',
                        background:
                          'linear-gradient(to bottom, transparent, #1e1e1e)',
                        pointerEvents: 'none',
                      }}
                    />
                  )}
                </div>
                {commandTruncated?.isTruncated && (
                  <div
                    style={{
                      color: '#1890ff',
                      cursor: 'pointer',
                      fontSize: '11px',
                      marginTop: '4px',
                      textDecoration: 'underline',
                    }}
                    onClick={() => setCommandExpanded(!commandExpanded)}>
                    {commandExpanded ? 'Show less' : 'Show more...'}
                  </div>
                )}
              </div>
            </div>
            {outputFocusText && (
              <div
                style={{
                  marginTop: 4,
                  paddingLeft: 20,
                  fontSize: '11px',
                  color: '#8c8c8c',
                  fontStyle: 'italic',
                }}>
                Focus: {outputFocusText}
              </div>
            )}
          </div>
        )}

        {status === 'executed' && (stdoutText || stderrText || outputText) && (
          <div
            style={{
              borderTop: '1px solid #333',
            }}>
            <div
              style={{
                display: 'flex',
                justifyContent: 'space-between',
                alignItems: 'center',
                padding: '5px 10px',
                borderBottom: '1px solid #333',
                background: '#1a1a1a',
              }}>
              <span style={{ fontSize: '11px', color: '#c4c4c4' }}>Output</span>
              <Copy
                style={{
                  color: '#c4c4c4',
                  cursor: 'pointer',
                  width: 14,
                  height: 14,
                }}
                onClick={async () => {
                  if (!outputTextForCopy) {
                    toast.warning('No output to copy');
                    return;
                  }
                  try {
                    await navigator.clipboard.writeText(outputTextForCopy);
                    toast.success('Copied to clipboard');
                  } catch (_err) {
                    toast.error('Failed to copy to clipboard');
                  }
                }}
              />
            </div>
            <div
              style={{
                padding: '5px 10px',
                maxHeight: outputExpanded ? '200px' : 'none',
                overflowY: outputExpanded ? 'scroll' : 'visible',
                overflowX: 'hidden',
                scrollbarWidth: 'thin',
                scrollbarColor: '#555 #2a2a2a',
              }}
              className="shell-output-container">
              {stdoutText &&
                renderOutputBlock(
                  'stdout',
                  stdoutText,
                  '#e8e8e8',
                  stdoutTruncated,
                )}
              {stderrText &&
                renderOutputBlock(
                  'stderr',
                  stderrText,
                  '#ff7875',
                  stderrTruncated,
                )}
              {!stdoutText &&
                !stderrText &&
                outputText &&
                renderOutputBlock(
                  'output',
                  outputText,
                  '#e8e8e8',
                  fallbackOutputTruncated,
                )}
              {isAnyOutputTruncated && (
                <div
                  style={{
                    color: '#1890ff',
                    cursor: 'pointer',
                    fontSize: '11px',
                    marginTop: '4px',
                    textDecoration: 'underline',
                  }}
                  onClick={() => setOutputExpanded(!outputExpanded)}>
                  {outputExpanded ? 'Show less' : 'Show more...'}
                </div>
              )}
            </div>
          </div>
        )}

        {status === 'executed' && focusResultText && (
          <div
            style={{
              borderTop: '1px solid #333',
            }}>
            <div
              style={{
                padding: '5px 10px',
                borderBottom: '1px solid #333',
                background: '#1a1a1a',
              }}>
              <span style={{ fontSize: '11px', color: '#c4c4c4' }}>
                Focus Result
              </span>
            </div>
            <div style={{ padding: '5px 10px' }}>
              {containsAnsi(focusResultText) ? (
                <pre
                  style={{
                    margin: 0,
                    fontSize: '12px',
                    color: '#b5cea8',
                    whiteSpace: 'pre-wrap',
                    wordBreak: 'break-word',
                    lineHeight: '1.5',
                  }}>
                  <span
                    dangerouslySetInnerHTML={{
                      __html: renderAnsiHtml(focusResultText),
                    }}
                  />
                </pre>
              ) : (
                <SyntaxHighlighter
                  language="bash"
                  style={vscDarkPlus}
                  customStyle={{
                    margin: 0,
                    padding: 0,
                    background: 'transparent',
                    fontSize: '12px',
                  }}
                  PreTag="div"
                  codeTagProps={{
                    style: { fontFamily: 'inherit', color: '#b5cea8' },
                  }}>
                  {focusResultText}
                </SyntaxHighlighter>
              )}
            </div>
          </div>
        )}

        {shouldShowTokens && (
          <div
            style={{
              borderTop: '1px solid #333',
              padding: '6px 10px',
              background: '#1a1a1a',
              color: '#c4c4c4',
              fontSize: '11px',
              display: 'flex',
              gap: 10,
              justifyContent: 'flex-end',
              flexWrap: 'wrap',
              alignItems: 'center',
            }}>
            <span>REQ: {formatRequestTokenUsageLine(requestTokenUsageIn)}</span>
            <TokenUsagePopoverIcon
              requestTokenUsageIn={requestTokenUsageIn}
              requestTokenUsageOut={requestTokenUsageOut}
              durationMs={durationMs}
            />
          </div>
        )}
      </div>
    </div>
  );
};
