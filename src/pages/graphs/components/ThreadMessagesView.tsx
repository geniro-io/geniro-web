// ─── Helper: filter trailing messages ────────────────────────────────────────

function filterTrailingMessages(
  messages: PreparedMessage[],
  hasResultText: boolean,
): PreparedMessage[] {
  if (!hasResultText || messages.length === 0) return messages;

  let filtered = messages;

  const last = filtered[filtered.length - 1];
  if (last.type === 'tool' && last.name?.toLowerCase() === 'finish') {
    filtered = filtered.slice(0, -1);
  }

  if (filtered.length > 0) {
    const newLast = filtered[filtered.length - 1];
    if (newLast.type === 'chat' && !newLast.isToolCallContent) {
      filtered = filtered.slice(0, -1);
    }
  }

  return filtered;
}

// ─── Helper: CollapsibleInnerArea ────────────────────────────────────────────

const COLLAPSED_MESSAGE_COUNT = 3;

function CollapsibleInnerArea({
  items,
  renderItem,
  isCalling,
  emptyText,
}: {
  items: PreparedMessage[];
  renderItem: (item: PreparedMessage, index: number) => React.ReactNode;
  isCalling: boolean;
  emptyText: string;
}) {
  const [expanded, setExpanded] = React.useState(false);

  const nonReasoningCount = items.filter((m) => m.type !== 'reasoning').length;
  const isCollapsible =
    !isCalling && nonReasoningCount > COLLAPSED_MESSAGE_COUNT;

  const visibleMessages = React.useMemo(() => {
    if (!isCollapsible || expanded) return items;
    const result: PreparedMessage[] = [];
    let count = 0;
    for (const m of items) {
      result.push(m);
      if (m.type !== 'reasoning') count++;
      if (count >= COLLAPSED_MESSAGE_COUNT) break;
    }
    return result;
  }, [items, isCollapsible, expanded]);

  const hiddenCount = nonReasoningCount - COLLAPSED_MESSAGE_COUNT;

  return (
    <div className="space-y-2 overflow-x-hidden">
      {items.length === 0 && isCalling && (
        <span className="text-xs italic text-muted-foreground">
          {emptyText}
        </span>
      )}
      {visibleMessages.map((item, idx) => (
        <div key={item.id || idx}>{renderItem(item, idx)}</div>
      ))}
      {isCollapsible && (
        <div
          onClick={() => setExpanded((prev) => !prev)}
          className="flex items-center gap-1 mt-1 cursor-pointer text-[11px] text-muted-foreground hover:text-foreground select-none transition-colors">
          {expanded ? (
            <>
              <ChevronDown className="w-[9px] h-[9px]" />
              Collapse
            </>
          ) : (
            <>
              <ChevronRight className="w-[9px] h-[9px]" />
              {hiddenCount} more message{hiddenCount !== 1 ? 's' : ''}
            </>
          )}
        </div>
      )}
    </div>
  );
}
