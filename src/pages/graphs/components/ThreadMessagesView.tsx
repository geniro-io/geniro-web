// ─── Shell tool display adapter ──────────────────────────────────────────────

function ShellToolDisplayAdapter({
  name,
  status,
  resultContent,
  shellCommand,
  toolOptions,
  title,
  requestTokenUsageIn,
  requestTokenUsageOut,
  durationMs,
}: {
  name: string;
  status: ToolRenderStatus;
  resultContent?: unknown;
  shellCommand?: string;
  toolOptions?: Record<string, JsonValue>;
  title?: string;
  requestTokenUsageIn?: ThreadMessageDtoRequestTokenUsage | null;
  requestTokenUsageOut?: ThreadMessageDtoRequestTokenUsage | null;
  durationMs?: number;
}) {
  const resultObj = isPlainObject(resultContent)
    ? (resultContent as Record<string, unknown>)
    : null;

  const toolErrorText = useMemo(() => {
    if (!resultObj) return undefined;
    return extractToolErrorText(resultObj);
  }, [resultObj]);

  const stdoutCandidate = resultObj?.stdout;
  const stdoutText = typeof stdoutCandidate === 'string' ? stdoutCandidate : '';
  const stderrCandidate = resultObj?.stderr;
  const stderrText =
    typeof stderrCandidate === 'string' ? stderrCandidate : undefined;
  const exitCodeCandidate = resultObj?.exitCode;
  const exitCode =
    typeof exitCodeCandidate === 'number' ? exitCodeCandidate : 0;
  const focusResultCandidate = resultObj?.focusResult;
  const focusResultText =
    typeof focusResultCandidate === 'string' && focusResultCandidate.trim()
      ? focusResultCandidate
      : undefined;
  const outputCandidate = resultObj?.output;
  const outputFieldText =
    typeof outputCandidate === 'string' ? outputCandidate : undefined;

  // Fallback: raw string result
  const rawStringResult =
    typeof resultContent === 'string' ? resultContent : undefined;

  // Build stdout from all possible sources
  const effectiveStdout =
    stdoutText || outputFieldText || rawStringResult || '';

  const shellStatus: 'executing' | 'executed' | 'error' =
    status === 'calling'
      ? 'executing'
      : toolErrorText || (exitCode !== 0 && exitCode !== null)
        ? 'error'
        : 'executed';

  const displayCommand = shellCommand || name;

  // Build popover content for detailed inspection
  const popoverContent = useMemo(() => {
    if (status === 'calling') return undefined;
    if (resultContent === undefined && !toolOptions) return undefined;

    return (
      <ToolPopoverPanel
        toolLabel={name}
        args={
          toolOptions ? (toolOptions as Record<string, unknown>) : undefined
        }
        resultContent={resultContent}
        usageIn={requestTokenUsageIn as RawTokenUsage | undefined}
        usageOut={requestTokenUsageOut as RawTokenUsage | undefined}
        durationMs={durationMs}
      />
    );
  }, [
    status,
    resultContent,
    toolOptions,
    name,
    requestTokenUsageIn,
    requestTokenUsageOut,
    durationMs,
  ]);

  return (
    <ShellBlock
      command={displayCommand}
      stdout={effectiveStdout}
      stderr={stderrText}
      focusResult={focusResultText}
      exitCode={exitCode}
      status={shellStatus}
      title={title || undefined}
      usageIn={requestTokenUsageIn as RawTokenUsage | undefined}
      usageOut={requestTokenUsageOut as RawTokenUsage | undefined}
      durationMs={durationMs}
      popoverContent={popoverContent}
    />
  );
}

// ─── Main component ──────────────────────────────────────────────────────────

const ThreadMessagesView: React.FC<ThreadMessagesViewProps> = React.memo(
  ({
    messages,
    messagesLoading,
    selectedThreadId,
    nodeId,
    nodeDisplayNames,
    showNodeHeadings = false,
    isAgentNode = true,
    nodeTemplateKind,
    onLoadMoreMessages,
    hasMoreMessages,
    loadingMore,
    isNodeRunning = false,
    isThreadStopped = false,
    currentThreadLastRunId,
    pendingMessages = [],
    newMessageMode,
  }) => {
    const scrollContainerRef = useRef<HTMLDivElement | null>(null);
    const isPrependingRef = useRef<boolean>(false);
    const justPrependedRef = useRef<boolean>(false);
    const pendingAutoScrollRef = useRef<boolean>(false);
    const autoScrollDisabledRef = useRef<boolean>(false);
    const lastMessageCountRef = useRef<number>(0);
    const anchorRef = useRef<{
      id: string;
      viewportOffset: number;
    } | null>(null);

    const isWorkGroupItem = useCallback((item: PreparedMessage): boolean => {
      if (item.type === 'reasoning') return true;
      if (
        item.type === 'tool' &&
        item.name?.toLowerCase() !== 'finish' &&
        item.name?.toLowerCase() !== 'proxy_finish'
      )
        return true;
      if (item.type === 'chat' && item.isToolCallContent) return true;
      return false;
    }, []);

    useEffect(() => {
      ensureThinkingIndicatorStyles();
    }, []);

    useEffect(() => {
      pendingAutoScrollRef.current = true;
    }, [selectedThreadId, nodeId]);

    useLayoutEffect(() => {
      if (!isPrependingRef.current) return;
      if (messages.length === lastMessageCountRef.current) return;

      isPrependingRef.current = false;
      justPrependedRef.current = true;
      lastMessageCountRef.current = messages.length;

      const el = scrollContainerRef.current;
      const anchor = anchorRef.current;
      if (!el || !anchor) return;

      const restoreScroll = () => {
        const target = el.querySelector<HTMLElement>(
          `[data-msg-id="${CSS.escape(anchor.id)}"]`,
        );
        if (target) {
          el.scrollTop = target.offsetTop - anchor.viewportOffset;
        }
      };

      restoreScroll();

      const observer = new ResizeObserver(() => restoreScroll());
      observer.observe(el);
      setTimeout(() => {
        observer.disconnect();
        anchorRef.current = null;
      }, 500);
    }, [messages.length]);

    useEffect(() => {
      if (isPrependingRef.current) return;
      if (justPrependedRef.current) {
        justPrependedRef.current = false;
        return;
      }
      if (messagesLoading) return;
      const el = scrollContainerRef.current;
      if (!el) return;

      const isInitiallyEmpty = lastMessageCountRef.current === 0;
      const shouldAutoScroll =
        pendingAutoScrollRef.current ||
        isInitiallyEmpty ||
        !autoScrollDisabledRef.current;

      if (shouldAutoScroll) {
        el.scrollTop = el.scrollHeight;
      }

      pendingAutoScrollRef.current = false;
      lastMessageCountRef.current = messages.length;
    }, [messagesLoading, messages.length]);

    useEffect(() => {
      if (!pendingMessages || pendingMessages.length === 0) return;
      const el = scrollContainerRef.current;
      if (!el) return;
      if (!autoScrollDisabledRef.current) {
        el.scrollTop = el.scrollHeight;
      }
    }, [pendingMessages]);

    useEffect(() => {
      const styleId = 'shell-scrollbar-styles';
      if (document.getElementById(styleId)) return;

      const style = document.createElement('style');
      style.id = styleId;
      style.textContent = `
      .shell-output-container::-webkit-scrollbar {
        width: 8px;
        height: 8px;
      }
      .shell-output-container::-webkit-scrollbar-track {
        background: #2a2a2a;
        border-radius: 4px;
      }
      .shell-output-container::-webkit-scrollbar-thumb {
        background: #555;
        border-radius: 4px;
      }
      .shell-output-container::-webkit-scrollbar-thumb:hover {
        background: #666;
      }
    `;
      document.head.appendChild(style);

      return () => {
        const existingStyle = document.getElementById(styleId);
        if (existingStyle) {
          existingStyle.remove();
        }
      };
    }, []);

    const handleScroll = useCallback(() => {
      const el = scrollContainerRef.current;
      if (!el) return;
      const nearTop = el.scrollTop <= 100;
      if (
        nearTop &&
        hasMoreMessages &&
        !loadingMore &&
        typeof onLoadMoreMessages === 'function'
      ) {
        const msgEls = el.querySelectorAll<HTMLElement>('[data-msg-id]');
        const lastEl = msgEls.length > 0 ? msgEls[msgEls.length - 1] : null;
        if (lastEl) {
          anchorRef.current = {
            id: lastEl.getAttribute('data-msg-id') ?? '',
            viewportOffset: lastEl.offsetTop - el.scrollTop,
          };
        }
        isPrependingRef.current = true;
        onLoadMoreMessages();
      }
      const nearBottom = el.scrollHeight - el.scrollTop - el.clientHeight <= 48;
      autoScrollDisabledRef.current = !nearBottom;
    }, [hasMoreMessages, loadingMore, onLoadMoreMessages]);

    const buildToolPopover = useCallback(
      (
        rawResult: unknown,
        rawArgs: unknown,
        toolLabel: string,
        tokenUsageIn?: ThreadMessageDtoRequestTokenUsage | null,
        tokenUsageOut?: ThreadMessageDtoRequestTokenUsage | null,
        durationMs?: number,
      ): React.ReactNode => (
        <ToolPopoverPanel
          toolLabel={toolLabel}
          args={
            rawArgs && typeof rawArgs === 'object'
              ? (rawArgs as Record<string, unknown>)
              : undefined
          }
          resultContent={rawResult}
          usageIn={tokenUsageIn as RawTokenUsage | undefined}
          usageOut={tokenUsageOut as RawTokenUsage | undefined}
          durationMs={durationMs}
        />
      ),
      [],
    );

    const preparedMessagesResult = useMemo(() => {
      return prepareReadyMessages(messages, {
        isNodeRunning,
        isThreadStopped,
        currentThreadLastRunId,
      });
    }, [messages, isNodeRunning, isThreadStopped, currentThreadLastRunId]);

    const isThinkingVisible = isNodeRunning && isAgentNode && !isThreadStopped;

    const lastAgentNodeId = useMemo(() => {
      for (let k = preparedMessagesResult.length - 1; k >= 0; k--) {
        const pm = preparedMessagesResult[k];
        const nid = pm.sourceAgentNodeId || pm.nodeId;
        if (nid) return nid;
      }
      return nodeId;
    }, [preparedMessagesResult, nodeId]);

    const lastAgentName = lastAgentNodeId
      ? nodeDisplayNames?.[lastAgentNodeId] ||
        `Node ${lastAgentNodeId.slice(-6)}`
      : 'Agent';

    const renderFullHeightState = (content: React.ReactNode) => (
      <div style={fullHeightColumnStyle}>
        <div style={centeredStateStyle}>{content}</div>
      </div>
    );

    const renderSystemMessage = (message: ThreadMessageDto) => {
      const content = formatMessageContent(message.message?.content);

      return (
        <div className="w-[90%] mx-auto">
          <div className="text-xs text-muted-foreground text-center">
            <MarkdownContent content={content} allowHorizontalScroll={true} />
          </div>
        </div>
      );
    };

    const renderFinishTool = (
      status: ToolRenderStatus,
      resultContent?: unknown,
      requestTokenUsage?: ThreadMessageDtoRequestTokenUsage | null,
      metadata?: { nodeId?: string; createdAt?: string; roleLabel?: string },
      durationMs?: number,
    ) => {
      if (status !== 'executed') {
        return undefined;
      }

      let finishMessage = 'Task completed.';
      let needsMoreInfo = false;
      if (resultContent) {
        if (typeof resultContent === 'string') {
          finishMessage = resultContent;
        } else if (isPlainObject(resultContent)) {
          const obj = resultContent as Record<string, unknown>;
          finishMessage =
            (typeof obj.message === 'string' ? obj.message : '') ||
            (typeof obj.content === 'string' ? obj.content : '') ||
            (typeof obj.result === 'string' ? obj.result : '') ||
            'Task completed.';
          needsMoreInfo = Boolean(obj.needsMoreInfo);
        }
      }

      const finishAgentName =
        (metadata?.nodeId && nodeDisplayNames?.[metadata.nodeId]) ||
        (metadata?.nodeId ? `Node ${metadata.nodeId.slice(-6)}` : 'Agent');

      const timestamp = formatMetadataLine(metadata?.createdAt) ?? '';

      const finishTokens = requestTokenUsage
        ? toTokenInfo(requestTokenUsage as RawTokenUsage, durationMs)
        : undefined;

      return (
        <FinishBlock
          sender={finishAgentName}
          color={needsMoreInfo ? 'bg-amber-500' : 'bg-green-500'}
          timestamp={timestamp}
          variant={needsMoreInfo ? 'need_more' : 'done'}
          message={finishMessage}
          tokens={finishTokens}
        />
      );
    };

    const renderToolStatusLine = (opts: {
      name: string;
      status: ToolRenderStatus;
      resultContent?: unknown;
      toolOptions?: Record<string, JsonValue>;
      titleText?: string;
      align?: 'left' | 'center';
      requestTokenUsageIn?: ThreadMessageDtoRequestTokenUsage | null;
      requestTokenUsageOut?: ThreadMessageDtoRequestTokenUsage | null;
      durationMs?: number;
    }) => {
      const toolErrorText =
        opts.status === 'executed'
          ? extractToolErrorText(opts.resultContent)
          : undefined;

      return (
        <ToolBlock
          toolName={opts.name}
          status="done"
          rawStatus={opts.status}
          resultContent={opts.resultContent}
          toolOptions={
            opts.toolOptions
              ? (opts.toolOptions as Record<string, unknown>)
              : undefined
          }
          titleText={opts.titleText}
          align={opts.align ?? 'center'}
          requestTokenUsageIn={
            opts.requestTokenUsageIn as RawTokenUsage | undefined
          }
          requestTokenUsageOut={
            opts.requestTokenUsageOut as RawTokenUsage | undefined
          }
          durationMs={opts.durationMs}
          errorText={toolErrorText}
        />
      );
    };

    const renderShellStatusLine = (opts: {
      name: string;
      status: ToolRenderStatus;
      resultContent?: unknown;
      shellCommand?: string;
      toolOptions?: Record<string, JsonValue>;
      metadata?: { nodeId?: string; createdAt?: string; roleLabel?: string };
      titleText?: string;
      requestTokenUsageIn?: ThreadMessageDtoRequestTokenUsage | null;
      requestTokenUsageOut?: ThreadMessageDtoRequestTokenUsage | null;
      durationMs?: number;
    }) => {
      return (
        <ShellToolDisplayAdapter
          name={opts.name}
          status={opts.status}
          resultContent={opts.resultContent}
          shellCommand={opts.shellCommand}
          toolOptions={opts.toolOptions}
          title={opts.titleText}
          requestTokenUsageIn={opts.requestTokenUsageIn}
          requestTokenUsageOut={opts.requestTokenUsageOut}
          durationMs={opts.durationMs}
        />
      );
    };

    const renderNonAgentNodeInfo = () => {
      if (isAgentNode) return null;

      return (
        <div style={{ padding: '16px', textAlign: 'center' }}>
          <div
            style={{
              display: 'flex',
              flexDirection: 'column',
              alignItems: 'center',
              gap: 16,
            }}>
            <Wrench style={{ width: 32, height: 32, color: '#1890ff' }} />
            <div>
              <span style={{ fontSize: '16px', fontWeight: 600 }}>
                {nodeTemplateKind || 'Tool'} Node
              </span>
              <br />
              <span style={{ fontSize: '14px', color: '#8c8c8c' }}>
                This node executes tool calls and doesn&apos;t participate in
                conversations.
              </span>
            </div>
            <div
              style={{
                backgroundColor: '#f8f9fa',
                border: '1px solid #e9ecef',
                borderRadius: '8px',
                padding: '12px',
                textAlign: 'left',
              }}>
              <span
                style={{ fontSize: '13px', color: '#495057', fontWeight: 600 }}>
                Tool Call Information:
              </span>
              <br />
              <span style={{ fontSize: '12px', color: '#8c8c8c' }}>
                {'\u2022'} Tool calls are executed when this node is triggered
                <br />
                {'\u2022'} Results are passed to connected nodes
                <br />
                {'\u2022'} Check the execution logs for detailed information
              </span>
            </div>
          </div>
        </div>
      );
    };

    const renderMessage = (message: ThreadMessageDto) => {
      const role = (message.message?.role as string) || '';
      const content = formatMessageContent(message.message?.content);
      if (isToolLikeRole(role)) {
        const name = getMessageString(message.message, 'name') || 'tool';
        const title = getMessageTitle(message.message);
        const resultContent = message.message?.content;
        return renderToolStatusLine({
          name,
          status: 'executed',
          resultContent,
          titleText: title,
          requestTokenUsageOut: message.requestTokenUsage,
          durationMs: extractDurationMs(message.message),
        });
      }

      if (isBlankContent(message.message?.content)) return null;

      const additional = getAdditionalKwargs(message.message);
      const isAgentInstruction = Boolean(
        additional?.__isAgentInstructionMessage ??
        additional?.isAgentInstructionMessage,
      );
      const isReportingMessage = Boolean(
        additional?.__isReportingMessage ?? additional?.isReportingMessage,
      );

      const isHuman = role === 'human';
      const avatarColor = isHuman
        ? 'bg-gray-500'
        : role === 'ai'
          ? 'bg-green-500'
          : role === 'system'
            ? 'bg-purple-500'
            : 'bg-gray-400';

      const agentName =
        (message.nodeId && nodeDisplayNames?.[message.nodeId]) ||
        (message.nodeId ? `Node ${message.nodeId.slice(-6)}` : 'Agent');

      const avatarTooltip = isHuman ? 'You' : agentName;

      if (isAgentInstruction) {
        return (
          <CommunicationBlock
            status="done"
            instructionContent={content}
            instructionLabel={`Providing Instructions for ${agentName}`}
            targetAgentName={agentName}>
            {null}
          </CommunicationBlock>
        );
      }

      if (isReportingMessage) {
        return (
          <ChatBubble
            sender={isHuman ? 'You' : agentName}
            role={isHuman ? 'human' : 'ai'}
            color={avatarColor}
            avatarTooltip={avatarTooltip}
            isReport
            content={content}
            copyContent={content}
            timestamp={message.createdAt}
            tokens={
              message.requestTokenUsage
                ? toTokenInfo(
                    message.requestTokenUsage as RawTokenUsage,
                    extractDurationMs(message.message),
                  )
                : undefined
            }
          />
        );
      }

      if (isHuman) {
        return (
          <ChatBubble
            sender="You"
            role="human"
            color={avatarColor}
            avatarTooltip={avatarTooltip}
            timestamp={message.createdAt}
            content={content}
            copyContent={content}
          />
        );
      }

      return (
        <ChatBubble
          sender={agentName}
          role="ai"
          color={avatarColor}
          avatarTooltip={avatarTooltip}
          timestamp={message.createdAt}
          tokens={
            message.requestTokenUsage
              ? toTokenInfo(
                  message.requestTokenUsage as RawTokenUsage,
                  extractDurationMs(message.message),
                )
              : undefined
          }
          content={content}
          copyContent={content}
        />
      );
    };

    const renderPendingMessage = (message: PendingMessage) => {
      const isHuman = message.role === 'human';
      const content = message.content;

      const sendTimeText =
        newMessageMode === 'inject_after_tool_call'
          ? 'Will be sent after next tool execution'
          : newMessageMode === 'wait_for_completion'
            ? 'Will be sent after agent completes current task'
            : 'Pending';

      const pendingAgentName =
        (nodeId && nodeDisplayNames?.[nodeId]) ||
        (nodeId ? `Node ${nodeId.slice(-6)}` : 'Agent');
      const pendingAvatarTooltip = isHuman ? 'You' : pendingAgentName;

      return (
        <ChatBubble
          sender={isHuman ? 'You' : pendingAgentName}
          role={isHuman ? 'human' : 'ai'}
          color={isHuman ? 'bg-gray-500' : 'bg-green-500'}
          avatarTooltip={pendingAvatarTooltip}
          isPending
          pendingNote={sendTimeText}
          content={content}
          copyContent={content}
        />
      );
    };

    const formatNodeLabel = (nodeIdentifier?: string): string | undefined => {
      if (!nodeIdentifier) {
        return undefined;
      }
      const mapped = nodeDisplayNames?.[nodeIdentifier];
      if (mapped && mapped.trim().length > 0) {
        return mapped;
      }
      if (nodeIdentifier.length <= 10) {
        return nodeIdentifier;
      }
      return `Node ${nodeIdentifier.slice(-6)}`;
    };

    const resolveSenderLabel = (
      roleLabel?: string,
      sourceNodeId?: string,
    ): string | undefined => {
      if (roleLabel === 'human') {
        return 'Human';
      }
      if (showNodeHeadings) {
        const label = formatNodeLabel(sourceNodeId || nodeId);
        if (label) {
          return label;
        }
      }
      return roleLabel;
    };

    const formatMetadataLine = (
      createdAt?: string,
      roleLabel?: string,
      sourceNodeId?: string,
    ): string | undefined => {
      const datePart = createdAt
        ? new Date(createdAt).toLocaleString()
        : undefined;
      const descriptorParts: string[] = [];
      const senderLabel = resolveSenderLabel(roleLabel, sourceNodeId);
      if (senderLabel) {
        descriptorParts.push(`from ${senderLabel}`);
      }
      const descriptor = descriptorParts.join(' ');
      const parts = [datePart, descriptor].filter(
        (part) => part && part.length,
      );
      if (!parts.length) {
        return undefined;
      }
      return parts.join(' | ');
    };

    const renderWorkingItem = (
      it: PreparedMessage,
      idx: number,
    ): React.ReactNode => {
      if (it.type === 'reasoning') {
        if (isBlankContent(it.message.message?.content)) return null;
        const content = limitConsecutiveNewlines(
          formatMessageContent(it.message.message?.content),
        );
        const additionalKwargs = (it.message.message?.additionalKwargs ??
          {}) as Record<string, unknown>;
        const isStreaming = Boolean(
          additionalKwargs?.[STREAMING_REASONING_FLAG],
        );

        return (
          <div key={`work-reasoning-${it.id}-${idx}`}>
            <StreamingReasoningBlock
              content={content}
              isStreaming={isStreaming}
              markdownStyle={{
                fontSize: '13px',
                lineHeight: '1.4',
                color: '#666',
              }}
            />
          </div>
        );
      }

      if (it.type === 'tool') {
        if (it.toolKind === 'shell') {
          return (
            <div key={`work-shell-${it.id}-${idx}`}>
              {renderShellStatusLine({
                name: it.name,
                status: it.status,
                resultContent: it.result,
                shellCommand: it.shellCommand,
                toolOptions: it.toolOptions,
                metadata: {
                  nodeId: it.nodeId,
                  createdAt: it.createdAt,
                  roleLabel: it.roleLabel ?? it.name,
                },
                titleText: it.title,
                requestTokenUsageIn: it.requestTokenUsageIn,
                requestTokenUsageOut: it.requestTokenUsageOut,
                durationMs: it.durationMs,
              })}
            </div>
          );
        }

        if (it.name && it.name.toLowerCase() === 'finish') {
          return (
            <div key={`work-finish-${it.id}-${idx}`}>
              {renderFinishTool(
                it.status,
                it.result,
                it.requestTokenUsage,
                {
                  nodeId: it.nodeId,
                  createdAt: it.createdAt,
                  roleLabel: 'ai',
                },
                it.durationMs,
              )}
            </div>
          );
        }

        return (
          <div key={`work-tool-${it.id}-${idx}`}>
            {renderToolStatusLine({
              name: it.name,
              status: it.status,
              resultContent: it.result,
              toolOptions: it.toolOptions,
              titleText: it.title,
              align: 'left',
              requestTokenUsageIn: it.requestTokenUsageIn,
              requestTokenUsageOut: it.requestTokenUsageOut,
              durationMs: it.durationMs,
            })}
          </div>
        );
      }

      if (it.type === 'system') {
        const sysContent = formatMessageContent(it.message.message?.content);
        return (
          <div
            key={`work-system-${it.id}-${idx}`}
            style={{ fontSize: '12px', color: '#afafaf' }}>
            <MarkdownContent
              content={sysContent}
              allowHorizontalScroll={true}
            />
          </div>
        );
      }

      if (it.type === 'chat') {
        const content = formatMessageContent(it.message.message?.content);
        if (isBlankContent(it.message.message?.content)) return null;
        return (
          <div
            key={`work-chat-${it.id}-${idx}`}
            style={{
              padding: '6px 10px',
              backgroundColor: '#ffffff',
              borderRadius: 6,
              border: '1px solid #f0f0f0',
            }}>
            <MarkdownContent content={content} style={MARKDOWN_COMPACT_STYLE} />
          </div>
        );
      }

      if (it.type === 'subagent') {
        const subPopover =
          it.status !== 'calling'
            ? buildToolPopover(
                it.rawToolResult,
                it.rawToolArgs,
                'subagents_run_task',
                it.requestTokenUsageIn,
                it.requestTokenUsageOut,
              )
            : undefined;

        const filteredInner = filterTrailingMessages(
          it.innerMessages,
          !!it.resultText,
        );
        const isCalling = it.status === 'calling';

        return (
          <div key={`work-subagent-${it.id}-${idx}`}>
            <SubagentBlock
              purpose={it.purpose}
              status={
                it.status === 'calling'
                  ? 'running'
                  : it.status === 'stopped'
                    ? 'error'
                    : it.errorText
                      ? 'error'
                      : 'done'
              }
              taskDescription={it.taskDescription}
              model={it.model}
              errorText={it.errorText}
              resultText={it.resultText}
              statistics={it.statistics}
              popoverContent={subPopover}
              usageIn={it.requestTokenUsageIn as RawTokenUsage | undefined}
              usageOut={it.requestTokenUsageOut as RawTokenUsage | undefined}
              showThinkingIndicator={isCalling && filteredInner.length > 0}>
              {(filteredInner.length > 0 || isCalling) && (
                <CollapsibleInnerArea
                  items={filteredInner}
                  renderItem={renderWorkingItem}
                  isCalling={isCalling}
                  emptyText="Subagent is working..."
                />
              )}
            </SubagentBlock>
          </div>
        );
      }

      if (it.type === 'communication') {
        const commTargetName =
          it.targetAgentName ||
          (it.targetNodeId && nodeDisplayNames?.[it.targetNodeId]) ||
          (it.targetNodeId ? `Node ${it.targetNodeId.slice(-6)}` : undefined);
        const sourceNodeId = it.sourceAgentNodeId || it.nodeId;
        const commSourceName = sourceNodeId
          ? nodeDisplayNames?.[sourceNodeId] || `Node ${sourceNodeId.slice(-6)}`
          : undefined;
        const commSourceColor = sourceNodeId
          ? getAgentColor(sourceNodeId)
          : undefined;
        const commTargetColorNodeId =
          it.targetNodeId || it.innerMessages.find((im) => im.nodeId)?.nodeId;
        const commTargetColor = commTargetColorNodeId
          ? getAgentColor(commTargetColorNodeId)
          : undefined;
        const commPopover =
          it.status !== 'calling'
            ? buildToolPopover(
                it.rawToolResult,
                it.rawToolArgs,
                'communication_exec',
                it.requestTokenUsageIn,
                it.requestTokenUsageOut,
              )
            : undefined;

        const parentContent = it.parentMessage?.message?.content
          ? formatMessageContent(it.parentMessage.message.content)
          : undefined;
        const instructionContent = it.instructionMessage?.message?.content
          ? formatMessageContent(it.instructionMessage.message.content)
          : undefined;

        const filteredInner = filterTrailingMessages(
          it.innerMessages,
          !!it.resultText,
        );
        const isCalling = it.status === 'calling';

        return (
          <div key={`work-comm-${it.id}-${idx}`}>
            <CommunicationBlock
              status={
                it.status === 'calling'
                  ? 'running'
                  : it.status === 'stopped'
                    ? 'error'
                    : it.errorText
                      ? 'error'
                      : 'done'
              }
              targetAgentName={commTargetName}
              sourceAgentName={commSourceName}
              sourceColor={commSourceColor}
              targetColor={commTargetColor}
              parentContent={
                parentContent ? (
                  <MarkdownContent content={parentContent} />
                ) : undefined
              }
              instructionContent={instructionContent}
              errorText={it.errorText}
              resultText={it.resultText}
              model={it.model}
              statistics={it.statistics}
              popoverContent={commPopover}
              usageIn={it.requestTokenUsageIn as RawTokenUsage | undefined}
              usageOut={it.requestTokenUsageOut as RawTokenUsage | undefined}
              showThinkingIndicator={isCalling && filteredInner.length > 0}
              thinkingText={
                commTargetName
                  ? `${commTargetName} is thinking...`
                  : 'Agent is thinking...'
              }>
              {(filteredInner.length > 0 || isCalling) && (
                <CollapsibleInnerArea
                  items={filteredInner}
                  renderItem={renderWorkingItem}
                  isCalling={isCalling}
                  emptyText="Agent is working..."
                />
              )}
            </CommunicationBlock>
          </div>
        );
      }

      return null;
    };

    const renderPreparedMessages = () => {
      const rows: React.ReactNode[] = [];
      let i = 0;

      const pushRow = (
        key: React.Key,
        content: React.ReactNode,
        extraStyle?: React.CSSProperties,
        msgId?: string,
      ) => {
        rows.push(
          <div
            key={key}
            data-msg-id={msgId ?? String(key)}
            style={{ ...messageBlockStyle, ...extraStyle }}>
            {content}
          </div>,
        );
      };

      const getAvatarInitials = (label?: string): string => {
        if (!label) return 'AI';
        const trimmed = label.trim();
        if (!trimmed) return 'AI';
        const words = trimmed.split(/\s+/).filter(Boolean);
        if (words.length === 1) {
          return words[0].slice(0, 2).toUpperCase();
        }
        return words
          .slice(0, 2)
          .map((w) => w[0])
          .join('')
          .toUpperCase();
      };

      const renderWorkingBlock = (items: PreparedMessage[]) => {
        const avatarSeedNodeId =
          items.find((it) => it.nodeId)?.nodeId ?? nodeId ?? undefined;
        const avatarLabel = getAvatarInitials(
          formatNodeLabel(avatarSeedNodeId) ?? undefined,
        );

        const workingAgentName =
          (avatarSeedNodeId && nodeDisplayNames?.[avatarSeedNodeId]) ||
          (avatarSeedNodeId ? `Node ${avatarSeedNodeId.slice(-6)}` : 'Agent');

        return (
          <ChatBubble
            sender={avatarLabel}
            role="ai"
            color="#8c8c8c"
            avatarTooltip={workingAgentName}
            isWorking
            content=""
            customBody={
              <div className="flex flex-col gap-1.5 w-full">
                <span className="text-xs font-semibold text-muted-foreground text-left">
                  Working...
                </span>
                <div className="flex flex-col gap-1.5">
                  {items.map((it, idx) => renderWorkingItem(it, idx))}
                </div>
              </div>
            }
          />
        );
      };

      while (i < preparedMessagesResult.length) {
        const item = preparedMessagesResult[i];

        if (item.type === 'system') {
          pushRow(item.id, renderSystemMessage(item.message));
          i++;
          continue;
        }

        if (item.type === 'reasoning') {
          const group: PreparedMessage[] = [item];
          let j = i + 1;
          while (
            j < preparedMessagesResult.length &&
            isWorkGroupItem(preparedMessagesResult[j])
          ) {
            group.push(preparedMessagesResult[j]);
            j++;
          }
          pushRow(`working-${item.id}`, renderWorkingBlock(group));
          i = j;
          continue;
        }

        if (item.type === 'chat') {
          if (item.isToolCallContent && isWorkGroupItem(item)) {
            const group: PreparedMessage[] = [item];
            let j = i + 1;
            while (
              j < preparedMessagesResult.length &&
              isWorkGroupItem(preparedMessagesResult[j])
            ) {
              group.push(preparedMessagesResult[j]);
              j++;
            }
            if (group.length === 1) {
              pushRow(item.id, renderMessage(item.message));
              i++;
              continue;
            }
            pushRow(`working-${item.id}`, renderWorkingBlock(group));
            i = j;
            continue;
          }
          pushRow(item.id, renderMessage(item.message));
          i++;
          continue;
        }

        if (item.type === 'subagent') {
          const topSubPopover =
            item.status !== 'calling'
              ? buildToolPopover(
                  item.rawToolResult,
                  item.rawToolArgs,
                  'subagents_run_task',
                  item.requestTokenUsageIn,
                  item.requestTokenUsageOut,
                )
              : undefined;

          const filteredInner = filterTrailingMessages(
            item.innerMessages,
            !!item.resultText,
          );
          const isCalling = item.status === 'calling';

          pushRow(
            item.id,
            <SubagentBlock
              purpose={item.purpose}
              status={
                item.status === 'calling'
                  ? 'running'
                  : item.status === 'stopped'
                    ? 'error'
                    : item.errorText
                      ? 'error'
                      : 'done'
              }
              taskDescription={item.taskDescription}
              model={item.model}
              errorText={item.errorText}
              resultText={item.resultText}
              statistics={item.statistics}
              popoverContent={topSubPopover}
              usageIn={item.requestTokenUsageIn as RawTokenUsage | undefined}
              usageOut={item.requestTokenUsageOut as RawTokenUsage | undefined}
              showThinkingIndicator={isCalling && filteredInner.length > 0}>
              {(filteredInner.length > 0 || isCalling) && (
                <CollapsibleInnerArea
                  items={filteredInner}
                  renderItem={renderWorkingItem}
                  isCalling={isCalling}
                  emptyText="Subagent is working..."
                />
              )}
            </SubagentBlock>,
          );
          i++;
          continue;
        }

        if (item.type === 'communication') {
          const commTargetName =
            item.targetAgentName ||
            (item.targetNodeId && nodeDisplayNames?.[item.targetNodeId]) ||
            (item.targetNodeId
              ? `Node ${item.targetNodeId.slice(-6)}`
              : undefined);
          const topSourceNodeId = item.sourceAgentNodeId || item.nodeId;
          const topCommSourceName = topSourceNodeId
            ? nodeDisplayNames?.[topSourceNodeId] ||
              `Node ${topSourceNodeId.slice(-6)}`
            : undefined;
          const topCommSourceColor = topSourceNodeId
            ? getAgentColor(topSourceNodeId)
            : undefined;
          const topCommTargetColorNodeId =
            item.targetNodeId ||
            item.innerMessages.find((im) => im.nodeId)?.nodeId;
          const topCommTargetColor = topCommTargetColorNodeId
            ? getAgentColor(topCommTargetColorNodeId)
            : undefined;
          const topCommPopover =
            item.status !== 'calling'
              ? buildToolPopover(
                  item.rawToolResult,
                  item.rawToolArgs,
                  'communication_exec',
                  item.requestTokenUsageIn,
                  item.requestTokenUsageOut,
                )
              : undefined;

          const parentContent = item.parentMessage?.message?.content
            ? formatMessageContent(item.parentMessage.message.content)
            : undefined;
          const instructionContent = item.instructionMessage?.message?.content
            ? formatMessageContent(item.instructionMessage.message.content)
            : undefined;

          const filteredInner = filterTrailingMessages(
            item.innerMessages,
            !!item.resultText,
          );
          const isCalling = item.status === 'calling';

          pushRow(
            item.id,
            <CommunicationBlock
              status={
                item.status === 'calling'
                  ? 'running'
                  : item.status === 'stopped'
                    ? 'error'
                    : item.errorText
                      ? 'error'
                      : 'done'
              }
              targetAgentName={commTargetName}
              sourceAgentName={topCommSourceName}
              sourceColor={topCommSourceColor}
              targetColor={topCommTargetColor}
              parentContent={
                parentContent ? (
                  <MarkdownContent content={parentContent} />
                ) : undefined
              }
              instructionContent={instructionContent}
              errorText={item.errorText}
              resultText={item.resultText}
              model={item.model}
              statistics={item.statistics}
              popoverContent={topCommPopover}
              usageIn={item.requestTokenUsageIn as RawTokenUsage | undefined}
              usageOut={item.requestTokenUsageOut as RawTokenUsage | undefined}
              showThinkingIndicator={isCalling && filteredInner.length > 0}
              thinkingText={
                commTargetName
                  ? `${commTargetName} is thinking...`
                  : 'Agent is thinking...'
              }>
              {(filteredInner.length > 0 || isCalling) && (
                <CollapsibleInnerArea
                  items={filteredInner}
                  renderItem={renderWorkingItem}
                  isCalling={isCalling}
                  emptyText="Agent is working..."
                />
              )}
            </CommunicationBlock>,
          );
          i++;
          continue;
        }

        if (item.type === 'tool') {
          if (isWorkGroupItem(item)) {
            const group: PreparedMessage[] = [item];
            let j = i + 1;
            while (
              j < preparedMessagesResult.length &&
              isWorkGroupItem(preparedMessagesResult[j])
            ) {
              group.push(preparedMessagesResult[j]);
              j++;
            }
            pushRow(`working-${item.id}`, renderWorkingBlock(group));
            i = j;
            continue;
          }
          if (item.name && item.name.toLowerCase() === 'finish') {
            pushRow(
              item.id || `finish-${i}`,
              renderFinishTool(
                item.status,
                item.result,
                item.requestTokenUsage,
                {
                  nodeId: item.nodeId,
                  createdAt: item.createdAt,
                  roleLabel: 'ai',
                },
                item.durationMs,
              ),
            );
            i++;
            continue;
          }

          if (item.toolKind === 'shell') {
            pushRow(
              item.id || `shell-${i}`,
              renderShellStatusLine({
                name: item.name,
                status: item.status,
                resultContent: item.result,
                shellCommand: item.shellCommand,
                toolOptions: item.toolOptions,
                metadata: {
                  nodeId: item.nodeId,
                  createdAt: item.createdAt,
                  roleLabel: item.roleLabel ?? item.name,
                },
                titleText: item.title,
                requestTokenUsageIn: item.requestTokenUsageIn,
                requestTokenUsageOut: item.requestTokenUsageOut,
                durationMs: item.durationMs,
              }),
            );
            i++;
            continue;
          }

          pushRow(
            item.id || `generic-tool-${i}`,
            renderToolStatusLine({
              name: item.name,
              status: item.status,
              resultContent: item.result,
              toolOptions: item.toolOptions,
              titleText: item.title,
              requestTokenUsageIn: item.requestTokenUsageIn,
              requestTokenUsageOut: item.requestTokenUsageOut,
              durationMs: item.durationMs,
            }),
          );
          i++;
          continue;
        }

        i++;
      }

      return rows;
    };

    if (!selectedThreadId) {
      return renderFullHeightState(
        <span>Select a thread from the header to view messages.</span>,
      );
    }

    if (!isAgentNode) {
      return (
        <div style={fullHeightColumnStyle}>
          <div
            style={{
              ...scrollContainerStyle,
              overflowX: 'auto',
            }}>
            {renderNonAgentNodeInfo()}
          </div>
        </div>
      );
    }

    if (messagesLoading) {
      return renderFullHeightState(
        <div
          style={{
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
            gap: 8,
          }}>
          <Loader2 className="size-6 animate-spin text-muted-foreground" />
          <span>Loading messages...</span>
        </div>,
      );
    }

    if (messages.length === 0) {
      return renderFullHeightState(
        <span>No messages found for this thread.</span>,
      );
    }

    return (
      <div style={{ ...fullHeightColumnStyle, position: 'relative' }}>
        {loadingMore && (
          <div
            style={{
              position: 'absolute',
              top: 0,
              left: 0,
              right: 0,
              zIndex: 1,
              padding: '8px 12px',
              textAlign: 'center',
              fontSize: '12px',
              color: '#8c8c8c',
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              gap: '8px',
              background:
                'linear-gradient(to bottom, rgba(255,255,255,0.95), rgba(255,255,255,0.7))',
            }}>
            <Loader2 className="size-4 animate-spin" />
            <span style={{ fontSize: '12px', color: '#8c8c8c' }}>
              Loading more messages...
            </span>
          </div>
        )}
        <div
          ref={scrollContainerRef}
          onScroll={handleScroll}
          style={scrollContainerStyle}>
          {renderPreparedMessages()}
          {isThinkingVisible && !messagesLoading && (
            <div
              style={{
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                gap: 8,
                padding: '4px 0',
                animation:
                  'messages-tab-thinking-pulse 1.6s ease-in-out infinite',
              }}>
              <Avatar className="size-5 shrink-0">
                <AvatarFallback className="bg-primary/20 text-primary text-[8px] font-semibold">
                  {getAgentInitials(lastAgentName)}
                </AvatarFallback>
              </Avatar>
              <div
                style={{
                  fontSize: '12px',
                  color: '#8c8c8c',
                  lineHeight: '1.5',
                }}>
                {lastAgentName} is thinking...
              </div>
            </div>
          )}
          {pendingMessages && pendingMessages.length > 0 && (
            <div
              style={{
                borderTop: '1px solid #ebebeb',
                paddingTop: '10px',
                marginTop: '8px',
              }}>
              {pendingMessages.map((msg, idx) => (
                <div
                  key={`pending-${msg.content}-${idx}`}
                  style={messageBlockStyle}>
                  {renderPendingMessage(msg)}
                </div>
              ))}
            </div>
          )}
        </div>
      </div>
    );
  },
  (prevProps, nextProps) => {
    const messagesEqual =
      prevProps.messages === nextProps.messages ||
      (prevProps.messages.length === nextProps.messages.length &&
        prevProps.messages.every(
          (msg, idx) =>
            msg.id === nextProps.messages[idx]?.id &&
            msg.message === nextProps.messages[idx]?.message,
        ));

    const pendingMessagesEqual =
      prevProps.pendingMessages === nextProps.pendingMessages ||
      (prevProps.pendingMessages?.length ===
        nextProps.pendingMessages?.length &&
        (prevProps.pendingMessages?.every(
          (msg, idx) =>
            msg.content === nextProps.pendingMessages?.[idx]?.content &&
            msg.role === nextProps.pendingMessages?.[idx]?.role,
        ) ??
          false));

    return (
      messagesEqual &&
      prevProps.messagesLoading === nextProps.messagesLoading &&
      prevProps.selectedThreadId === nextProps.selectedThreadId &&
      prevProps.nodeId === nextProps.nodeId &&
      prevProps.nodeDisplayNames === nextProps.nodeDisplayNames &&
      prevProps.showNodeHeadings === nextProps.showNodeHeadings &&
      prevProps.isAgentNode === nextProps.isAgentNode &&
      prevProps.nodeTemplateKind === nextProps.nodeTemplateKind &&
      prevProps.hasMoreMessages === nextProps.hasMoreMessages &&
      prevProps.loadingMore === nextProps.loadingMore &&
      prevProps.isNodeRunning === nextProps.isNodeRunning &&
      prevProps.isThreadStopped === nextProps.isThreadStopped &&
      prevProps.currentThreadLastRunId === nextProps.currentThreadLastRunId &&
      pendingMessagesEqual &&
      prevProps.newMessageMode === nextProps.newMessageMode &&
      prevProps.onLoadMoreMessages === nextProps.onLoadMoreMessages
    );
  },
);

