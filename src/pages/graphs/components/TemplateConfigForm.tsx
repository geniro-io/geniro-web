const RjsfArrayAsTextareaField = (
  props: FieldProps<FormData, RJSFSchema, FormContext>,
) => {
  const current = Array.isArray(props.formData)
    ? (props.formData as unknown as string[])
    : [];
  return (
    <div>
      <span className="text-sm font-semibold">
        {props.schema.title ?? props.name}
      </span>
      {props.schema.description && (
        <span
          className="text-muted-foreground"
          style={{ display: 'block', fontSize: 12, marginBottom: 8 }}>
          {props.schema.description}
        </span>
      )}
      <ArrayTagInput
        value={current}
        onChange={(lines) => {
          props.onChange(lines as unknown as FormData, props.fieldPathId.path);
        }}
        disabled={props.disabled || props.readonly}
        placeholder="Enter item and press Enter"
      />
    </div>
  );
};

const RjsfKeyValueObjectField = (
  props: FieldProps<FormData, RJSFSchema, FormContext>,
) => {
  const record = useMemo(
    () =>
      props.formData &&
      typeof props.formData === 'object' &&
      !Array.isArray(props.formData)
        ? (props.formData as Record<string, unknown>)
        : {},
    [props.formData],
  );

  // Local state keeps pairs with empty keys visible while the user is typing.
  // We seed from formData on mount and re-sync when formData changes externally.
  const [localPairs, setLocalPairs] = useState<KeyValuePair[]>(() =>
    Object.entries(record).map(([k, v]) => ({
      key: k,
      value: v === undefined || v === null ? '' : String(v),
    })),
  );

  // Sync from formData when it changes externally (e.g. undo, reset).
  // Only overwrite if the serialised record actually differs from what
  // localPairs would produce, so we don't clobber in-progress edits.
  const prevRecordRef = React.useRef(record);
  React.useEffect(() => {
    if (prevRecordRef.current === record) return;
    prevRecordRef.current = record;

    const fromLocal: Record<string, string> = {};
    for (const p of localPairs) {
      const k = p.key.trim();
      if (k) fromLocal[k] = p.value;
    }
    if (JSON.stringify(fromLocal) !== JSON.stringify(record)) {
      setLocalPairs(
        Object.entries(record).map(([k, v]) => ({
          key: k,
          value: v === undefined || v === null ? '' : String(v),
        })),
      );
    }
  }, [record, localPairs]);

  const handleChange = (nextPairs: KeyValuePair[]) => {
    setLocalPairs(nextPairs);
    const next: Record<string, string> = {};
    for (const pair of nextPairs) {
      const k = pair.key.trim();
      if (!k) continue;
      next[k] = pair.value;
    }
    props.onChange(next as unknown as FormData, props.fieldPathId.path);
  };

  return (
    <div>
      <span className="text-sm font-semibold">
        {props.schema.title ?? props.name}
      </span>
      {props.schema.description && (
        <span
          className="text-muted-foreground"
          style={{ display: 'block', fontSize: 12 }}>
          {props.schema.description}
        </span>
      )}
      <KeyValueInput value={localPairs} onChange={handleChange} />
    </div>
  );
};

// --- Stable RJSF config objects (module-level, never re-created) ---

// RJSF @rjsf/core Form requires all config objects to match its inferred T
// generic (FormData). Our widgets use narrower value types (string, number,
// boolean) which are correct at runtime but conflict with the strict generic
// constraint. We cast through unknown to satisfy TypeScript while preserving
// full type safety inside each widget/field implementation.
type RjsfTemplates = Partial<TemplatesType<FormData, RJSFSchema, FormContext>>;
type RjsfWidgets = RegistryWidgetsType<FormData, RJSFSchema, FormContext>;
type RjsfFields = RegistryFieldsType<FormData, RJSFSchema, FormContext>;

const RJSF_TEMPLATES: RjsfTemplates = {
  BaseInputTemplate: RjsfBaseInputTemplate,
  ObjectFieldTemplate: RjsfObjectFieldTemplate,
  FieldTemplate: RjsfFieldTemplate,
} as unknown as RjsfTemplates;

const RJSF_WIDGETS: RjsfWidgets = {
  TextWidget: RjsfTextWidget,
  SelectWidget: RjsfSelectWidget,
  NumberWidget: RjsfNumberWidget,
  UpDownWidget: RjsfUpDownWidget,
  CheckboxWidget: RjsfCheckboxWidget,
  textWithAi: RjsfTextWithAiWidget,
  textareaWithExpand: RjsfTextareaWithExpandWidget,
  litellmModelsSelect: RjsfLitellmModelsSelectWidget,
} as unknown as RjsfWidgets;

const RJSF_FIELDS: RjsfFields = {
  arrayAsTextarea: RjsfArrayAsTextareaField,
  keyValueObject: RjsfKeyValueObjectField,
} as unknown as RjsfFields;

// --- Component ---

export interface TemplateConfigFormProps {
  schema: RjsfSchema;
  formData: FormData;
  onChange: (next: FormData) => void;
  liteLlmModels: LiteLlmModelDto[];
  litellmModelsLoading: boolean;
  onOpenExpandedTextarea: (fieldKey: string, value: string) => void;
  onOpenAiSuggestion: (
    fieldKey: string,
    fieldLabel: string,
    value?: unknown,
  ) => void;
  aiSuggestionEnabled: boolean;
  githubAppEnabled?: boolean;
  templateId?: string;
}

export const TemplateConfigForm: React.FC<TemplateConfigFormProps> = ({
  schema,
  formData,
  onChange,
  liteLlmModels,
  litellmModelsLoading,
  onOpenExpandedTextarea,
  onOpenAiSuggestion,
  aiSuggestionEnabled,
  githubAppEnabled = false,
  templateId,
}) => {
  const uiSchema = useMemo(() => buildUiSchema(schema), [schema]);

  const formContext: FormContext = useMemo(
    () => ({
      liteLlmModels,
      litellmModelsLoading,
      openExpandedTextarea: onOpenExpandedTextarea,
      openAiSuggestion: onOpenAiSuggestion,
      aiSuggestionEnabled,
      githubAppEnabled,
      formData,
    }),
    [
      aiSuggestionEnabled,
      formData,
      githubAppEnabled,
      liteLlmModels,
      litellmModelsLoading,
      onOpenAiSuggestion,
      onOpenExpandedTextarea,
    ],
  );

  const isGitHubNode =
    templateId === 'github-resource' || templateId === 'gh-tool';
  const showGitHubAppWarning = isGitHubNode && !githubAppEnabled;

  return (
    <>
      {showGitHubAppWarning && (
        <Alert variant="destructive" className="mb-3">
          <AlertTriangle className="size-4" />
          <AlertTitle>GitHub App not connected</AlertTitle>
          <AlertDescription>
            GitHub authentication requires a GitHub App. Go to Settings &gt;
            Integrations to install and link the GitHub App.
          </AlertDescription>
        </Alert>
      )}
      <Form
        schema={schema}
        uiSchema={uiSchema}
        formData={formData}
        formContext={formContext}
        validator={validator}
        showErrorList={false}
        noHtml5Validate
        liveValidate={false}
        templates={RJSF_TEMPLATES}
        widgets={RJSF_WIDGETS}
        fields={RJSF_FIELDS}
        onChange={(e) => {
          const next = (e.formData ?? {}) as FormData;
          onChange(next);
        }}>
        {/* No submit button in this UI (changes are propagated live) */}
        <></>
      </Form>
    </>
  );
};
