import { BarChart3, History, Loader2 } from 'lucide-react';
import React from 'react';

import type {
  ThreadUsageStatisticsDto,
  ThreadUsageStatisticsDtoSchema0,
} from '../../../autogenerated';
import { Badge } from '../../../components/ui/badge';
import { Button } from '../../../components/ui/button';
import { Separator } from '../../../components/ui/separator';
import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from '../../../components/ui/table';
import {
  Tooltip,
  TooltipContent,
  TooltipTrigger,
} from '../../../components/ui/tooltip';

/** Tool row with a stable key and optional children for tree table. */
interface ToolRow extends ThreadUsageStatisticsDtoSchema0 {
  key: string;
  children?: ToolRow[];
  depth?: number;
}

/** Recursively transform byTool entries into rows with unique keys and `children`. */
const toToolRows = (
  items: ThreadUsageStatisticsDtoSchema0[],
  parentKey = '',
  depth = 0,
): ToolRow[] =>
  items.map((item, idx) => {
    const key = parentKey ? `${parentKey}-${idx}` : `tool-${idx}`;
    return {
      ...item,
      key,
      depth,
      children:
        item.subCalls && item.subCalls.length > 0
          ? toToolRows(item.subCalls, key, depth + 1)
          : undefined,
    };
  });

/** Flatten a tree of ToolRow into a flat array for rendering. */
const flattenToolRows = (rows: ToolRow[]): ToolRow[] => {
  const result: ToolRow[] = [];
  for (const row of rows) {
    result.push(row);
    if (row.children) {
      result.push(...flattenToolRows(row.children));
    }
  }
  return result;
};

interface ThreadUsageDisplayProps {
  usage: ThreadUsageStatisticsDto | null;
  loading: boolean;
  error: string | null;
  nodeDisplayNames?: Record<string, string>;
  nodeKinds?: Record<string, string>;
  localEventsCount?: number;
  onShowLocalEvents?: () => void;
}

const formatNumber = (num?: number | null): string => {
  if (typeof num !== 'number' || !Number.isFinite(num)) return '\u2014';
  return new Intl.NumberFormat('en-US').format(num);
};

const formatUsd = (amount?: number | null): string => {
  if (typeof amount !== 'number' || !Number.isFinite(amount)) return '$\u2014';
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    minimumFractionDigits: 4,
    maximumFractionDigits: 4,
  }).format(amount);
};

const StatItem: React.FC<{ label: string; value: string }> = ({
  label,
  value,
}) => (
  <div className="flex flex-col">
    <span className="text-xs text-muted-foreground">{label}</span>
    <span className="text-base font-medium">{value}</span>
  </div>
);

export const ThreadUsageDisplay: React.FC<ThreadUsageDisplayProps> = ({
  usage,
  loading,
  error,
  nodeDisplayNames = {},
  nodeKinds = {},
  localEventsCount,
  onShowLocalEvents,
}) => {
  if (loading) {
    return (
      <div className="border rounded-lg p-3 mt-2">
        <div className="flex items-center gap-2">
          <Loader2 className="size-4 animate-spin" />
          <span className="text-sm text-muted-foreground">
            Loading usage statistics...
          </span>
        </div>
      </div>
    );
  }

  if (error) {
    return (
      <div className="border rounded-lg p-3 mt-2">
        <span className="text-sm text-destructive">{error}</span>
      </div>
    );
  }

  if (!usage) {
    return null;
  }

  const { total, requests, byNode, byTool, toolsAggregate, userMessageCount } =
    usage;

  // Prepare byNode data
  const byNodeData = byNode
    ? Object.entries(byNode).map(([nodeId, stats]) => ({
        nodeId,
        nodeName: nodeDisplayNames[nodeId] || nodeId,
        nodeKind: nodeKinds[nodeId] || 'Node',
        ...stats,
      }))
    : [];

  // Transform byTool into tree-compatible rows with unique keys
  const toolRows: ToolRow[] = byTool ? toToolRows(byTool) : [];

  // Check if any tool has execution cost
  const anyToolHasOwnCost = toolRows.some(
    (r) =>
      (typeof r.toolTokens === 'number' && r.toolTokens > 0) ||
      (typeof r.toolPrice === 'number' && r.toolPrice > 0),
  );

  const flatToolRows = flattenToolRows(toolRows);

  const hasLocalEvents =
    typeof localEventsCount === 'number' && localEventsCount > 0;

  return (
    <div className="border rounded-lg mt-2">
      {/* Header */}
      <div className="flex items-center justify-between px-3 py-2 border-b">
        <div className="flex items-center gap-2">
          <BarChart3 className="size-4" />
          <span className="text-sm font-semibold">Thread Usage Statistics</span>
        </div>
        {hasLocalEvents && onShowLocalEvents && (
          <Tooltip>
            <TooltipTrigger asChild>
              <Button variant="ghost" size="icon" onClick={onShowLocalEvents}>
                <History className="size-4" />
              </Button>
            </TooltipTrigger>
            <TooltipContent>
              View {localEventsCount} socket events
            </TooltipContent>
          </Tooltip>
        )}
      </div>

      <div className="p-3">
        {/* Total Statistics */}
        <div className="mb-4">
          <span className="text-sm font-semibold block mb-2">Total Usage</span>
          <div className="flex flex-wrap gap-6">
            {typeof requests === 'number' && (
              <StatItem label="Requests" value={formatNumber(requests)} />
            )}
            {typeof userMessageCount === 'number' && (
              <StatItem
                label="User Messages"
                value={formatNumber(userMessageCount)}
              />
            )}
            <StatItem
              label="Total Tokens"
              value={formatNumber(total.totalTokens)}
            />
            <StatItem
              label="Input Tokens"
              value={formatNumber(total.inputTokens)}
            />
            {typeof total.cachedInputTokens === 'number' && (
              <StatItem
                label="Cached Input"
                value={formatNumber(total.cachedInputTokens)}
              />
            )}
            <StatItem
              label="Output Tokens"
              value={formatNumber(total.outputTokens)}
            />
            {typeof total.reasoningTokens === 'number' && (
              <StatItem
                label="Reasoning"
                value={formatNumber(total.reasoningTokens)}
              />
            )}
            {typeof total.totalPrice === 'number' && (
              <StatItem
                label="Total Cost"
                value={formatUsd(total.totalPrice)}
              />
            )}
            {typeof total.currentContext === 'number' && (
              <StatItem
                label="Current Context"
                value={formatNumber(total.currentContext)}
              />
            )}
          </div>
        </div>

        <Separator className="my-3" />

        {/* Tools Aggregate */}
        {toolsAggregate && (
          <>
            <div className="mb-4">
              <span className="text-sm font-semibold block mb-2">
                Tools Aggregate
              </span>
              <div className="flex flex-wrap gap-4">
                <StatItem
                  label="Requests"
                  value={formatNumber(toolsAggregate.requestCount)}
                />
                <StatItem
                  label="Tokens"
                  value={formatNumber(toolsAggregate.totalTokens)}
                />
                {typeof toolsAggregate.totalPrice === 'number' && (
                  <StatItem
                    label="Cost"
                    value={formatUsd(toolsAggregate.totalPrice)}
                  />
                )}
              </div>
            </div>
            <Separator className="my-3" />
          </>
        )}

        {/* Usage by Agent */}
        {byNodeData.length > 0 && (
          <div className="mb-4">
            <span className="text-sm font-semibold block mb-3">
              Usage by Agent ({byNodeData.length})
            </span>
            <Table>
              <TableHeader>
                <TableRow>
                  <TableHead>Agent</TableHead>
                  <TableHead className="text-right">Total Tokens</TableHead>
                  <TableHead className="text-right">Input</TableHead>
                  <TableHead className="text-right">Output</TableHead>
                  <TableHead className="text-right">Cached</TableHead>
                  <TableHead className="text-right">Reasoning</TableHead>
                  <TableHead className="text-right">Total Price</TableHead>
                </TableRow>
              </TableHeader>
              <TableBody>
                {byNodeData.map((record) => (
                  <TableRow key={record.nodeId}>
                    <TableCell>
                      <div>
                        <div className="flex items-center gap-1.5">
                          <span className="font-semibold text-sm">
                            {record.nodeName}
                          </span>
                          <Badge variant="outline" className="text-[10px]">
                            {record.nodeKind}
                          </Badge>
                        </div>
                        {record.nodeName !== record.nodeId && (
                          <span className="text-xs text-muted-foreground">
                            {record.nodeId}
                          </span>
                        )}
                      </div>
                    </TableCell>
                    <TableCell className="text-right">
                      {formatNumber(record.totalTokens)}
                    </TableCell>
                    <TableCell className="text-right">
                      {formatNumber(record.inputTokens)}
                    </TableCell>
                    <TableCell className="text-right">
                      {formatNumber(record.outputTokens)}
                    </TableCell>
                    <TableCell className="text-right">
                      {typeof record.cachedInputTokens === 'number' &&
                      record.cachedInputTokens > 0
                        ? formatNumber(record.cachedInputTokens)
                        : '-'}
                    </TableCell>
                    <TableCell className="text-right">
                      {typeof record.reasoningTokens === 'number' &&
                      record.reasoningTokens > 0
                        ? formatNumber(record.reasoningTokens)
                        : '-'}
                    </TableCell>
                    <TableCell className="text-right">
                      {formatUsd(record.totalPrice)}
                    </TableCell>
                  </TableRow>
                ))}
              </TableBody>
            </Table>
          </div>
        )}

        {/* Usage by Tool */}
        {flatToolRows.length > 0 && (
          <>
            <Separator className="my-3" />
            <div className="mb-4">
              <span className="text-sm font-semibold block mb-3">
                Usage by Tool ({byTool.length})
              </span>
              <Table>
                <TableHeader>
                  <TableRow>
                    <TableHead>Tool Name</TableHead>
                    <TableHead className="text-right">Call Count</TableHead>
                    <TableHead className="text-right">LLM Tokens</TableHead>
                    <TableHead className="text-right">LLM Price</TableHead>
                    {anyToolHasOwnCost && (
                      <>
                        <TableHead className="text-right">
                          Tool Tokens
                        </TableHead>
                        <TableHead className="text-right">Tool Price</TableHead>
                      </>
                    )}
                  </TableRow>
                </TableHeader>
                <TableBody>
                  {flatToolRows.map((row) => (
                    <TableRow key={row.key}>
                      <TableCell>
                        <span
                          style={{
                            paddingLeft: (row.depth ?? 0) * 20,
                          }}>
                          {row.toolName}
                        </span>
                      </TableCell>
                      <TableCell className="text-right">
                        {formatNumber(row.callCount)}
                      </TableCell>
                      <TableCell className="text-right">
                        {formatNumber(row.totalTokens)}
                      </TableCell>
                      <TableCell className="text-right">
                        {formatUsd(row.totalPrice)}
                      </TableCell>
                      {anyToolHasOwnCost && (
                        <>
                          <TableCell className="text-right">
                            {typeof row.toolTokens === 'number' &&
                            row.toolTokens > 0
                              ? formatNumber(row.toolTokens)
                              : '-'}
                          </TableCell>
                          <TableCell className="text-right">
                            {typeof row.toolPrice === 'number' &&
                            row.toolPrice > 0
                              ? formatUsd(row.toolPrice)
                              : '-'}
                          </TableCell>
                        </>
                      )}
                    </TableRow>
                  ))}
                </TableBody>
              </Table>
            </div>
          </>
        )}
      </div>
    </div>
  );
};
